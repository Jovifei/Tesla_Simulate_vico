        metrics.steady_limits.mass_balance_limit), ...
    checkMaximum("energy_balance_residual", metrics.energy_balance_residual, ...
        metrics.steady_limits.energy_balance_limit), ...
    checkMaximum("source_balanced_momentum_residual", ...
        metrics.source_balanced_momentum_residual, ...
        metrics.steady_limits.momentum_balance_limit), ...
    checkMaximum("stagnation_temperature_spread", ...
        metrics.stagnation_temperature_spread, ...
        metrics.steady_limits.stagnation_temperature_spread_limit), ...
    checkMaximum("uniform_friction_decay_error", ...
        metrics.uniform_friction_decay_relative_error, 1e-11), ...
