function result = s12_benchmark_smooth_spatial(config, cflLimit)
%S12_BENCHMARK_SMOOTH_SPATIAL Measure cell-average spatial convergence.
arguments
    config (1,1) struct
    cflLimit (1,1) double {mustBePositive}
end

gamma = 1.4;
domainLength = 1.0;
velocity = 1.0;
pressure = 1.0;
amplitude = 0.2;
counts = reshape(config.cell_counts, 1, []);
runCount = numel(counts);
rhoError = zeros(1, runCount);
velocityError = zeros(1, runCount);
pressureError = zeros(1, runCount);
requestedDt = zeros(1, runCount);
effectiveDt = zeros(1, runCount);
stepCount = zeros(1, runCount);
cflClipped = false(1, runCount);
endTimeClipped = false(1, runCount);
solverRuns = cell(1, runCount);
started = tic;

for runIndex = 1:runCount
    cellCount = counts(runIndex);
    dx = domainLength / cellCount;
    x = ((1:cellCount) - 0.5) * dx;
    rhoInitial = cellAverageDensity(x, dx, 0.0, amplitude);
    initialState = s12_benchmark_primitive_to_conservative( ...
        rhoInitial, velocity, pressure, gamma);
    requestedDt(runIndex) = config.time_cfl * dx;
    stepCount(runIndex) = config.end_time / requestedDt(runIndex);
    if abs(stepCount(runIndex) - round(stepCount(runIndex))) > ...
            32 * eps(max(1, stepCount(runIndex)))
        error("S12:Benchmark:NonintegralSpatialTimeGrid", ...
            "Smooth spatial requested dt must divide end time exactly.");
    end
    stepCount(runIndex) = round(stepCount(runIndex));
    run = s12_run_periodic_ssprk3(initialState, gamma, dx, ...
        requestedDt(runIndex), stepCount(runIndex), cflLimit, ...
        Reconstruction=config.reconstruction);
    solverRuns{runIndex} = run;
    effectiveDt(runIndex) = max(run.used_dt);
    cflClipped(runIndex) = any(abs(run.used_dt - requestedDt(runIndex)) > ...
        32 * eps(max(1, requestedDt(runIndex))));
    endTimeClipped(runIndex) = abs(stepCount(runIndex) * ...
        requestedDt(runIndex) - config.end_time) > ...
        32 * eps(max(1, config.end_time));
    [rho, velocityNumeric, pressureNumeric] = ...
        s12_benchmark_conservative_to_primitive(run.final_state, gamma);
    rhoExact = cellAverageDensity(x, dx, config.end_time, amplitude);
    rhoError(runIndex) = mean(abs(rho - rhoExact));
    velocityError(runIndex) = mean(abs(velocityNumeric - velocity));
    pressureError(runIndex) = mean(abs(pressureNumeric - pressure));
end

result = struct( ...
    "id", "smooth_periodic_entropy_wave_spatial", ...
    "boundary", "periodic", ...
    "cell_counts", counts, ...
    "end_time", config.end_time, ...
    "time_cfl", config.time_cfl, ...
    "reconstruction", string(config.reconstruction), ...
    "uses_cell_averages", true, ...
    "rho_l1_error", rhoError, ...
    "velocity_l1_error", velocityError, ...
    "pressure_l1_error", pressureError, ...
    "observed_order", struct( ...
        "rho", log2(rhoError(1:end-1) ./ rhoError(2:end)), ...
        "velocity", log2(velocityError(1:end-1) ./ velocityError(2:end)), ...
        "pressure", log2(pressureError(1:end-1) ./ pressureError(2:end))), ...
    "dt_requested", requestedDt, ...
    "dt_effective", effectiveDt, ...
    "step_count", stepCount, ...
    "cfl_clipped", cflClipped, ...
    "end_time_clipped", endTimeClipped, ...
    "runtime_seconds", toc(started));
result.qualification = s12_benchmark_aggregate_qualification([solverRuns{:}]);
end

function density = cellAverageDensity(x, dx, time, amplitude)
attenuation = sin(pi * dx) / (pi * dx);
density = 1 + amplitude * attenuation * sin(2 * pi * (x - time));
end
