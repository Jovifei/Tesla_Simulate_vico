function result = run_s12_muscl_final_qualification(mode, options)
%RUN_S12_MUSCL_FINAL_QUALIFICATION Compare frozen first-order and MUSCL runs.
arguments
    mode (1,1) string {mustBeMember(mode, ["run", "report-only"])} = "run"
    options.Profile (1,1) string = "full"
    options.OutputDirectory (1,1) string = ""
    options.SourceManifest (1,1) string = ""
end
root = fileparts(mfilename("fullpath"));
if options.OutputDirectory == ""
    options.OutputDirectory = fullfile(root, "out", "sprint2-qualification", ...
        options.Profile);
end
if mode == "report-only"
    if options.SourceManifest == "" || ~isfile(options.SourceManifest)
        error("S12:Benchmark:MissingManifest", ...
            "Qualification report-only requires an existing SourceManifest.");
    end
    result = normalize(jsondecode(fileread(options.SourceManifest)));
    result = s12_write_sprint2_qualification_artifacts(result, ...
        options.OutputDirectory);
    copyfile(options.SourceManifest, fullfile(options.OutputDirectory, ...
        "benchmark-result.json"));
    return
end

firstDirectory = fullfile(options.OutputDirectory, "first_order");
musclDirectory = fullfile(options.OutputDirectory, "muscl_minmod");
first = run_s12_benchmarks("all", Profile=options.Profile, ...
    OutputDirectory=firstDirectory, Reconstruction="first_order");
muscl = run_s12_benchmarks("all", Profile=options.Profile, ...
    OutputDirectory=musclDirectory, Reconstruction="muscl_minmod");
result = buildResult(first, muscl, options.Profile);
result = s12_write_sprint2_qualification_artifacts(result, ...
    options.OutputDirectory);
end

function result = buildResult(first, muscl, profile)
if numel(first.cases) ~= numel(muscl.cases) || ...
        any(string({first.cases.id}) ~= string({muscl.cases.id}))
    error("S12:Benchmark:QualificationCaseMismatch", ...
        "First-order and MUSCL suites must contain the same ordered cases.");
end
count = numel(first.cases);
cases = repmat(struct("id", "", "category", "", "first_order", ...
    struct(), "muscl_minmod", struct(), "rho_error_ratio", NaN), 1, count);
for index = 1:count
    cases(index) = struct( ...
        "id", first.cases(index).id, ...
        "category", first.cases(index).category, ...
        "first_order", first.cases(index).metrics, ...
        "muscl_minmod", muscl.cases(index).metrics, ...
        "rho_error_ratio", rhoErrorRatio(first.cases(index).metrics, ...
            muscl.cases(index).metrics));
end
checks = qualificationChecks(first, muscl, cases);
if all([checks.passed])
    status = "passed";
else
    status = "failed";
end
result = struct( ...
    "schema", "benchmark.schema.v1", ...
    "schema_minor", 1, ...
    "suite", struct("profile", profile, ...
        "selector", "sprint2_muscl_final_qualification"), ...
    "environment", muscl.environment, ...
    "source_results", struct( ...
        "first_order_acceptance", first.acceptance.status, ...
        "muscl_minmod_acceptance", muscl.acceptance.status), ...
    "cases", cases, ...
    "artifacts", struct([]), ...
    "acceptance", struct("status", status, "checks", checks));
end

function checks = qualificationChecks(first, muscl, cases)
spatial = cases(string({cases.id}) == "smooth_periodic_entropy_wave_spatial");
blast = cases(string({cases.id}) == "woodward_colella_blast_wave");
if isempty(spatial) || isempty(blast)
    error("S12:Benchmark:QualificationCaseMissing", ...
        "Sprint 2 qualification requires spatial and blast-wave cases.");
end
checks = [ ...
    check("first_order_suite", first.acceptance.status == "passed"), ...
    check("muscl_suite", muscl.acceptance.status == "passed"), ...
    check("smooth_spatial_first_order", spatial.first_order.rho_observed_order(end) >= 0.7 && ...
        spatial.first_order.rho_observed_order(end) <= 1.3), ...
    check("smooth_spatial_muscl_order", spatial.muscl_minmod.rho_observed_order(end) >= 1.7 && ...
        spatial.muscl_minmod.rho_observed_order(end) <= 2.3), ...
    check("smooth_spatial_muscl_lower_error", ...
        all(spatial.muscl_minmod.rho_l1_error < spatial.first_order.rho_l1_error)), ...
    check("smooth_spatial_time_grid", ...
        ~any(spatial.first_order.cfl_clipped) && ...
        ~any(spatial.muscl_minmod.cfl_clipped) && ...
        ~any(spatial.first_order.end_time_clipped) && ...
        ~any(spatial.muscl_minmod.end_time_clipped)), ...
    check("all_diagnostics_honest", diagnosticsHonest(cases)), ...
    check("blast_wave_positive", blastWavePositive(blast.muscl_minmod))];
end

function value = rhoErrorRatio(first, muscl)
if isfield(first, "rho_l1_error") && isfield(muscl, "rho_l1_error")
    value = muscl.rho_l1_error ./ first.rho_l1_error;
elseif isfield(first, "density_l1_error") && isfield(muscl, "density_l1_error")
    value = muscl.density_l1_error ./ first.density_l1_error;
else
    value = NaN;
end
end

function value = diagnosticsHonest(cases)
required = ["minimum_reconstructed_density", ...
    "minimum_reconstructed_pressure", "invalid_reconstruction_count", ...
    "invalid_stage_count", "clipping_count", "flux_fallback_count", ...
    "automatic_retry_count", "cfl_changed"];
value = true;
for index = 1:numel(cases)
    for metrics = [cases(index).first_order, cases(index).muscl_minmod]
        if ~all(isfield(metrics, required)) || ...
                metrics.minimum_reconstructed_density <= 0 || ...
                metrics.minimum_reconstructed_pressure <= 0 || ...
                metrics.invalid_reconstruction_count ~= 0 || ...
                metrics.invalid_stage_count ~= 0 || ...
                metrics.clipping_count ~= 0 || ...
                metrics.flux_fallback_count ~= 0 || ...
                metrics.automatic_retry_count ~= 0 || metrics.cfl_changed
            value = false;
            return
        end
        if isfield(metrics, "has_nonfinite") && metrics.has_nonfinite
            value = false;
            return
        end
    end
end
end

function value = blastWavePositive(metrics)
value = metrics.min_density > 0 && metrics.min_pressure > 0 && ...
    metrics.minimum_reconstructed_density > 0 && ...
    metrics.minimum_reconstructed_pressure > 0 && ...
    metrics.invalid_reconstruction_count == 0 && ...
    metrics.invalid_stage_count == 0 && ~metrics.has_nonfinite;
end

function value = check(id, passed)
value = struct("id", id, "passed", logical(passed));
end

function result = normalize(result)
result.schema = string(result.schema);
result.suite.profile = string(result.suite.profile);
result.suite.selector = string(result.suite.selector);
result.acceptance.status = string(result.acceptance.status);
for index = 1:numel(result.cases)
    result.cases(index).id = string(result.cases(index).id);
    result.cases(index).category = string(result.cases(index).category);
end
end
