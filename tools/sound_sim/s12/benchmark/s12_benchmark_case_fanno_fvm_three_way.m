function raw = runCase(config)
base = config.physical_definition;
started = tic;
uniformDecay = runUniformDecay(config);
coldStart = runColdStart(config);
simscape = callValidation("s12_run_fanno_pipe_g", base);
runCount = numel(config.lengths_m) * numel(config.grid_cell_counts);
runs = repmat(emptyRun(), 1, runCount);
runIndex = 0;
for lengthIndex = 1:numel(config.lengths_m)
    lengthM = config.lengths_m(lengthIndex);
    for gridIndex = 1:numel(config.grid_cell_counts)
        cellCount = config.grid_cell_counts(gridIndex);
        reference = callValidation("s12_fanno_analytical_profile", ...
            base, lengthM, cellCount, config.quadrature_nodes);
        adapterConfig = adapterConfiguration(config, reference, lengthM);
        fvm = callValidation("s12_run_fanno_fvm", adapterConfig);
        runIndex = runIndex + 1;
        runs(runIndex) = struct("length_index", lengthIndex, ...
            "grid_index", gridIndex, "length_m", lengthM, ...
            "grid_cell_count", cellCount, "reference", reference, ...
            "fvm", fvm);
    end
end
raw = struct("config", config, "uniform_decay", uniformDecay, ...
    "cold_start", coldStart, ...
    "simscape", simscape, "runs", runs, ...
    "runtime_seconds", toc(started));
end

