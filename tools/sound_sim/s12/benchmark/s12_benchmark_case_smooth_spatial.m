function definition = s12_benchmark_case_smooth_spatial
%S12_BENCHMARK_CASE_SMOOTH_SPATIAL Define cell-average spatial convergence.
definition = struct( ...
    "configure", @configureCase, ...
    "run", @runCase, ...
    "analyze", @analyzeCase, ...
    "accept", @acceptCase);
end

function config = configureCase(profile)
config = profile.smooth_spatial;
config.cell_counts = reshape(config.cell_counts, 1, []);
config.cfl = profile.cfl_limit;
config.reconstruction = profile.reconstruction;
config.models = ["s12_euler_fvm_periodic_step_ref.slx", ...
    "s12_euler_fvm_periodic_step_muscl_minmod_ref.slx", ...
    "s12_euler_ssprk3_periodic_ref.slx"];
end

function raw = runCase(config)
raw = s12_benchmark_smooth_spatial(config, config.cfl);
end

function analysis = analyzeCase(raw)
metrics = struct( ...
    "cell_counts", raw.cell_counts, ...
    "rho_l1_error", raw.rho_l1_error, ...
    "velocity_l1_error", raw.velocity_l1_error, ...
    "pressure_l1_error", raw.pressure_l1_error, ...
    "rho_observed_order", raw.observed_order.rho, ...
    "velocity_observed_order", raw.observed_order.velocity, ...
    "pressure_observed_order", raw.observed_order.pressure, ...
    "observed_order", raw.observed_order.rho, ...
    "dt_requested", raw.dt_requested, ...
    "dt_effective", raw.dt_effective, ...
    "step_count", raw.step_count, ...
    "cfl_clipped", raw.cfl_clipped, ...
    "end_time_clipped", raw.end_time_clipped, ...
    "uses_cell_averages", raw.uses_cell_averages, ...
    "spatial_scheme", raw.qualification.spatial_scheme, ...
    "reconstruction_variables", raw.qualification.reconstruction_variables, ...
    "limiter", raw.qualification.limiter, ...
    "limiter_activation_count", raw.qualification.limiter_activation_count, ...
    "limited_cell_fraction", raw.qualification.limited_cell_fraction, ...
    "minimum_reconstructed_density", raw.qualification.minimum_reconstructed_density, ...
    "minimum_reconstructed_pressure", raw.qualification.minimum_reconstructed_pressure, ...
    "invalid_reconstruction_count", raw.qualification.invalid_reconstruction_count, ...
    "invalid_stage_count", raw.qualification.invalid_stage_count, ...
    "stage_validation_count", raw.qualification.stage_validation_count, ...
    "end_time_clipping_count", raw.qualification.end_time_clipping_count, ...
    "clipping_count", raw.qualification.clipping_count, ...
    "flux_fallback_count", raw.qualification.flux_fallback_count, ...
    "automatic_retry_count", raw.qualification.automatic_retry_count, ...
    "cfl_changed", raw.qualification.cfl_changed, ...
    "runtime_seconds", raw.runtime_seconds);
analysis = struct("metrics", metrics, "plot", struct( ...
    "x", raw.cell_counts, "error", raw.rho_l1_error));
end

function acceptance = acceptCase(metrics)
order = metrics.rho_observed_order(end);
if metrics.spatial_scheme == "first_order"
    lower = 0.7;
    upper = 1.3;
else
    lower = 1.7;
    upper = 2.3;
end
checks = [ ...
    check("smooth_spatial_cell_averages", metrics.uses_cell_averages), ...
    check("smooth_spatial_cfl_not_clipped", ~any(metrics.cfl_clipped)), ...
    check("smooth_spatial_no_end_time_clip", ~any(metrics.end_time_clipped)), ...
    check("smooth_spatial_rho_monotonic", ...
        all(diff(metrics.rho_l1_error) < 0)), ...
    check("smooth_spatial_rho_order_lower", order >= lower), ...
    check("smooth_spatial_rho_order_upper", order <= upper), ...
    check("smooth_spatial_interface_positive", ...
        metrics.minimum_reconstructed_density > 0 && ...
        metrics.minimum_reconstructed_pressure > 0), ...
    check("smooth_spatial_reconstruction_valid", ...
        metrics.invalid_reconstruction_count == 0)];
if all([checks.passed])
    status = "passed";
else
    status = "failed";
end
acceptance = struct("status", status, "checks", checks);
end

function value = check(id, passed)
value = struct("id", id, "passed", logical(passed), ...
    "actual", double(~passed), "limit", 0);
end
