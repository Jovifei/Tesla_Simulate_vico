function [pressure, velocity, arrival] = referenceTrace(definition, item, position, time)
definition.pressure_amplitude = item.amplitude_pa;
definition.wave_speed = sqrt(definition.gamma * definition.ambient_pressure / definition.ambient_density);
pressure = zeros(size(time)); velocity = zeros(size(time));
component = "incident";
if item.boundary == "closed_rigid_end", component = "closed_rigid_reflected"; end
if item.boundary == "ideal_pressure_release_open_end", component = "ideal_pressure_release_reflected"; end
for index = 1:numel(time)
    incident = callTransient("s12_transient_wave_linear_reference", definition, position, time(index), "incident");
    pressure(index) = incident.pressure; velocity(index) = incident.velocity_perturbation;
    if component ~= "incident"
        reflected = callTransient("s12_transient_wave_linear_reference", definition, position, time(index), component);
        pressure(index) = pressure(index) + reflected.pressure_perturbation;
        velocity(index) = velocity(index) + reflected.velocity_perturbation;
    end
end
arrival = callTransient("s12_transient_wave_arrival_time", time, ...
    pressure - definition.ambient_pressure, 0.1);
end

function windows = reflectionWindows(definition, item, position)
c = sqrt(definition.gamma * definition.ambient_pressure / definition.ambient_density);
incident = (position - definition.pulse_center) / c;
reflected = (2 * definition.pipe_length - definition.pulse_center - position) / c;
halfWidth = 3 * definition.pulse_sigma / c;
windows = struct("incident", [max(0, incident - halfWidth), incident + halfWidth], ...
    "reflected", [reflected - halfWidth, reflected + halfWidth], ...
    "ambient_pressure", definition.ambient_pressure, ...
    "ambient_density", definition.ambient_density, "wave_speed", c);
if item.boundary == "nonreflecting_reference_boundary"
    error("S12:TransientWave:NoReflectionWindow", "Nonreflecting case has no reflection metric.");
end
end

function value = simscapeComparison(raw, fvmArrival, referenceArrival, ambientPressure)
value = repmat(struct("boundary_type", "", "arrival_time_measured", NaN, ...
    "arrival_time_error_to_analytical", NaN, "arrival_time_difference_to_fvm", NaN, ...
    "waveform_l1_difference_to_fvm", NaN, "waveform_linf_difference_to_fvm", NaN, ...
    "sample_count", 0, "pressure_range_pa", NaN, "model_path", "", ...
    "alignment_id", "source_to_initial_profile_time_origin.v1"), 1, numel(raw.simscape));
for index = 1:numel(raw.simscape)
    item = raw.simscape(index); trace = item.result;
    arrival = callTransient("s12_transient_wave_arrival_time", trace.time_s.', ...
        (trace.pressure_pa - ambientPressure).', 0.1);
    arrival = arrival - trace.fvm_time_origin_s;
    fvmRun = raw.runs(find([raw.runs.case_index] == item.case_index, 1, "last")).fvm;
    alignedTime = trace.time_s - trace.fvm_time_origin_s;
    overlap = alignedTime >= fvmRun.trace_time_s(1) & ...
        alignedTime <= fvmRun.trace_time_s(end);
    simscapePressure = interp1(alignedTime(overlap), trace.pressure_pa(overlap), ...
        fvmRun.trace_time_s, "linear", "extrap");
    difference = simscapePressure - fvmRun.trace_pressure_pa( ...
        raw.config.comparison_probe_index, :);
    amplitude = raw.config.cases(item.case_index).amplitude_pa;
    value(index) = struct("boundary_type", trace.boundary_type, ...
        "arrival_time_measured", arrival, ...
        "arrival_time_error_to_analytical", arrival - referenceArrival(item.case_index), ...
        "arrival_time_difference_to_fvm", arrival - fvmArrival(item.case_index), ...
        "waveform_l1_difference_to_fvm", mean(abs(difference)) / amplitude, ...
        "waveform_linf_difference_to_fvm", max(abs(difference)) / amplitude, ...
        "sample_count", trace.sample_count, ...
        "pressure_range_pa", max(trace.pressure_pa) - min(trace.pressure_pa), ...
        "model_path", trace.model_path, ...
        "alignment_id", "source_to_initial_profile_time_origin.v1");
end
end

function acceptance = acceptCase(metrics)
limits = metrics.acceptance_limits;
linearRows = metrics.reference_applicability;
finite = all(isfinite(metrics.waveform_l1_error(linearRows, :)), "all") && ...
    all(isfinite(metrics.minimum_density), "all") && all(isfinite(metrics.minimum_pressure), "all");
closed = find(metrics.boundary_type == "closed_rigid_end", 1);
open = find(metrics.boundary_type == "ideal_pressure_release_open_end", 1);
checks = [ ...
    check("finite_metrics", finite, 0, 0), ...
    check("positive_density", min(metrics.minimum_density, [], "all") > 0, min(metrics.minimum_density, [], "all"), 0), ...
    check("positive_pressure", min(metrics.minimum_pressure, [], "all") > 0, min(metrics.minimum_pressure, [], "all"), 0), ...
    check("maximum_cfl", max(metrics.maximum_cfl, [], "all") <= limits.maximum_cfl, max(metrics.maximum_cfl, [], "all"), limits.maximum_cfl), ...
    check("no_retry", max(metrics.retry_count, [], "all") == 0, max(metrics.retry_count, [], "all"), 0), ...
    check("no_rejected_step", max(metrics.rejected_step_count, [], "all") == 0, max(metrics.rejected_step_count, [], "all"), 0), ...
    check("valid_reconstruction", max(metrics.invalid_reconstruction_count, [], "all") == 0, max(metrics.invalid_reconstruction_count, [], "all"), 0), ...
    check("valid_rk_stage", max(metrics.invalid_stage_count, [], "all") == 0, max(metrics.invalid_stage_count, [], "all"), 0), ...
    check("no_clipping", max(metrics.clipping_count, [], "all") == 0, max(metrics.clipping_count, [], "all"), 0), ...
    check("no_flux_fallback", max(metrics.flux_fallback_count, [], "all") == 0, max(metrics.flux_fallback_count, [], "all"), 0), ...
    check("closed_pressure_sign", metrics.pressure_reflection_coefficient(closed,end) > 0, metrics.pressure_reflection_coefficient(closed,end), 0), ...
    check("open_pressure_sign", metrics.pressure_reflection_coefficient(open,end) < 0, metrics.pressure_reflection_coefficient(open,end), 0), ...
    check("simscape_traces", all([metrics.simscape.sample_count] > 10), min([metrics.simscape.sample_count]), 10)];
if isfield(limits, "maximum_linear_waveform_l1_finest")
    linearRows = metrics.reference_applicability;
    reflectionMagnitude = abs([metrics.pressure_reflection_coefficient(closed, end), ...
        metrics.pressure_reflection_coefficient(open, end)]);
    simscapeArrival = abs([metrics.simscape.arrival_time_difference_to_fvm]);
    simscapeWaveform = [metrics.simscape.waveform_l1_difference_to_fvm];
    checks(end + 1:end + 7) = [ ...
        check("linear_waveform_finest", ...
        max(metrics.waveform_l1_error(linearRows, end)) <= limits.maximum_linear_waveform_l1_finest, ...
        max(metrics.waveform_l1_error(linearRows, end)), limits.maximum_linear_waveform_l1_finest), ...
        check("arrival_finest", ...
        max(abs(metrics.arrival_time_error(linearRows, end))) <= limits.maximum_abs_arrival_error_s_finest, ...
        max(abs(metrics.arrival_time_error(linearRows, end))), limits.maximum_abs_arrival_error_s_finest), ...
        check("linear_grid_monotone", ...
        all(diff(metrics.waveform_l1_error(linearRows, :), 1, 2) < 0, "all"), ...
        max(diff(metrics.waveform_l1_error(linearRows, :), 1, 2), [], "all"), 0), ...
        check("reflection_magnitude_finest", ...
        min(reflectionMagnitude) >= limits.minimum_reflection_magnitude_finest, ...
        min(reflectionMagnitude), limits.minimum_reflection_magnitude_finest), ...
        check("reflection_energy_finest", ...
        max(metrics.boundary_energy_residual([closed, open], end)) <= ...
        limits.maximum_boundary_energy_residual_finest, ...
        max(metrics.boundary_energy_residual([closed, open], end)), ...
        limits.maximum_boundary_energy_residual_finest), ...
        check("simscape_arrival_secondary", ...
        max(simscapeArrival) <= limits.maximum_simscape_fvm_arrival_difference_s, ...
        max(simscapeArrival), limits.maximum_simscape_fvm_arrival_difference_s), ...
        check("simscape_waveform_secondary", ...
        max(simscapeWaveform) <= limits.maximum_simscape_fvm_waveform_l1, ...
        max(simscapeWaveform), limits.maximum_simscape_fvm_waveform_l1)];
end
status = "failed"; if all([checks.passed]), status = "passed"; end
acceptance = struct("status", status, "checks", checks);
end

function value = check(id, passed, actual, limit)
value = struct("id", id, "passed", logical(passed), "actual", actual, "limit", limit);
end

function values = observedOrders(errors, grids)
values = NaN(size(errors,1), max(1,size(errors,2)-1));
for index = 1:size(errors,2)-1
    values(:,index) = log(errors(:,index) ./ errors(:,index+1)) / log(grids(index+1)/grids(index));
end
end

