function definition = s12_benchmark_case_smooth_periodic
%S12_BENCHMARK_CASE_SMOOTH_PERIODIC Define the smooth-wave case contract.
definition = struct( ...
    "configure", @configureCase, ...
    "run", @runCase, ...
    "analyze", @analyzeCase, ...
    "accept", @acceptCase);
end

function config = configureCase(profile)
config = profile.smooth;
config.models = ["s12_euler_fvm_periodic_step_ref.slx", ...
    "s12_euler_ssprk3_periodic_ref.slx"];
config.cfl = profile.cfl_limit;
config.reconstruction = profile.reconstruction;
end

function raw = runCase(config)
raw = s12_benchmark_smooth_periodic(config, config.cfl);
end

function analysis = analyzeCase(raw)
metrics = struct( ...
    "dt_requested", raw.dt_requested, ...
    "step_count", raw.step_count, ...
    "self_error", raw.self_error, ...
    "observed_order", raw.observed_order, ...
    "exact_error", raw.exact_error, ...
    "conservation_error", max(raw.scaled_conservation_error, [], "all"), ...
    "cfl_clipped", raw.cfl_clipped, ...
    "stage_dt_ratio", raw.stage_dt_ratio, ...
    "runtime_seconds", raw.runtime_seconds);
analysis = struct("metrics", metrics, "plot", struct( ...
    "x", raw.dt_requested(1:end - 1), "error", raw.self_error));
end

function acceptance = acceptCase(metrics)
finestOrder = metrics.observed_order(end);
checks = [ ...
    check("smooth_dt_not_clipped", ~any(metrics.cfl_clipped), ...
        double(any(metrics.cfl_clipped)), 0), ...
    check("smooth_same_stage_dt", all(abs(metrics.stage_dt_ratio - 1) ...
        <= 1e-12), max(abs(metrics.stage_dt_ratio - 1)), 1e-12), ...
    check("smooth_order_lower", finestOrder >= 2.7, finestOrder, 2.7), ...
    check("smooth_order_upper", finestOrder <= 3.3, finestOrder, 3.3), ...
    check("smooth_conservation", metrics.conservation_error <= 1e-11, ...
        metrics.conservation_error, 1e-11)];
acceptance = struct("status", passStatus(checks), "checks", checks);
end

function value = check(id, passed, actual, limit)
value = struct("id", id, "passed", passed, ...
    "actual", actual, "limit", limit);
end

function status = passStatus(checks)
if all([checks.passed])
    status = "passed";
else
    status = "failed";
end
end
