function comparison = fiveStationComparison(raw, config)
fractions = reshape(raw.simscape.segmented.station_positions_fraction, 1, []);
lengthCount = numel(config.lengths_m);
stationCount = numel(fractions);
quantityCount = 4;
analytical = zeros(lengthCount, stationCount, quantityCount);
simscape = zeros(lengthCount, stationCount, quantityCount);
fvm = zeros(lengthCount, stationCount, quantityCount);
base = config.physical_definition;
for lengthIndex = 1:lengthCount
    lengthM = config.lengths_m(lengthIndex);
    for stationIndex = 1:stationCount
        reference = callValidation("s12_fanno_reference", struct( ...
            "static_pressure", base.inlet_static_pressure, ...
            "static_temperature", base.inlet_static_temperature, ...
            "mach", raw.simscape.inlet_mach, "gamma", base.gamma, ...
            "gas_constant", base.gas_constant, "area", base.area, ...
            "diameter", base.diameter, ...
            "length", lengthM * fractions(stationIndex), ...
            "darcy_friction_factor", raw.simscape.darcy_friction_factor));
        analytical(lengthIndex, stationIndex, :) = outletVector(reference.outlet);
    end
    pressure = raw.simscape.segmented.station_static_pressure(lengthIndex, :);
    temperature = raw.simscape.segmented.station_static_temperature(lengthIndex, :);
    density = pressure ./ (base.gas_constant * temperature);
    mach = raw.simscape.segmented.station_mach(lengthIndex, :);
    simscape(lengthIndex, :, :) = cat(3, density, pressure, temperature, mach);
    entry = raw.runs(find([raw.runs.length_index] == lengthIndex & ...
        [raw.runs.grid_index] == numel(config.grid_cell_counts), 1));
    numerical = primitive(entry.fvm.final_state, base);
    positions = lengthM * fractions;
    centers = entry.reference.cell_centers_m;
    for stationIndex = 1:stationCount - 1
        fvm(lengthIndex, stationIndex, :) = [ ...
            interp1(centers, numerical.density, positions(stationIndex)), ...
            interp1(centers, numerical.pressure, positions(stationIndex)), ...
            interp1(centers, numerical.temperature, positions(stationIndex)), ...
            interp1(centers, numerical.mach, positions(stationIndex))];
    end
    fvm(lengthIndex, stationCount, :) = outletVector(entry.fvm.outlet);
end
comparison = struct("positions_fraction", fractions, ...
    "analytical", analytical, "simscape", simscape, "fvm", fvm, ...
    "fvm_analytical_relative_error", relativeDifference(fvm, analytical), ...
    "simscape_analytical_relative_error", ...
        relativeDifference(simscape, analytical), ...
    "fvm_simscape_relative_difference", relativeDifference(fvm, simscape));
end

function values = outletVector(outlet)
if isfield(outlet, "static_density")
    values = [outlet.static_density, outlet.static_pressure, ...
        outlet.static_temperature, outlet.mach];
else
    values = [outlet.density, outlet.pressure, outlet.temperature, outlet.mach];
end
end

function values = relativeDifference(first, second)
values = abs(first - second) ./ max(abs(second), realmin);
end

