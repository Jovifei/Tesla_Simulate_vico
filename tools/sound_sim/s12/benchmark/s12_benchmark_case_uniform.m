function definition = s12_benchmark_case_uniform
%S12_BENCHMARK_CASE_UNIFORM Define the uniform-state case contract.
definition = struct( ...
    "configure", @configureCase, ...
    "run", @runCase, ...
    "analyze", @analyzeCase, ...
    "accept", @acceptCase);
end

function config = configureCase(profile)
config = struct( ...
    "model", "s12_euler_ssprk3_sod_ref.slx", ...
    "gamma", 1.4, ...
    "cell_count", profile.uniform.cell_count, ...
    "dx", 1 / profile.uniform.cell_count, ...
    "end_time", profile.uniform.end_time, ...
    "cfl", profile.cfl_limit, ...
    "reconstruction", profile.reconstruction, ...
    "max_steps", 10000);
end

function raw = runCase(config)
rho = 1.1 * ones(1, config.cell_count);
velocity = 0.2 * ones(1, config.cell_count);
pressure = ones(1, config.cell_count);
initialState = primitiveToConservative(rho, velocity, pressure, config.gamma);
started = tic;
solver = s12_run_transmissive_ssprk3(initialState, config.gamma, ...
    config.dx, config.end_time, config.cfl, config.max_steps, ...
    Reconstruction=config.reconstruction);
raw = struct("config", config, "initial_state", initialState, ...
    "solver", solver, "runtime_seconds", toc(started));
end

function analysis = analyzeCase(raw)
[rho, velocity, pressure] = s12_benchmark_conservative_to_primitive( ...
    raw.solver.final_state, raw.config.gamma);
[rhoInitial, velocityInitial, pressureInitial] = ...
    s12_benchmark_conservative_to_primitive(raw.initial_state, raw.config.gamma);
scale = max(abs(raw.config.dx * sum(raw.initial_state, 2)).', 1);
metrics = struct( ...
    "max_state_error", max(abs(raw.solver.final_state - ...
        raw.initial_state), [], "all"), ...
    "density_l1_error", mean(abs(rho - rhoInitial)), ...
    "velocity_l1_error", mean(abs(velocity - velocityInitial)), ...
    "pressure_l1_error", mean(abs(pressure - pressureInitial)), ...
    "min_density", min(rho), ...
    "min_pressure", min(pressure), ...
    "density_total_variation", sum(abs(diff(rho))), ...
    "conservation_error", max(abs( ...
        raw.solver.conservation_residual) ./ scale), ...
    "max_courant", raw.solver.max_courant, ...
    "step_count", raw.solver.step_count, ...
    "runtime_seconds", raw.runtime_seconds);
metrics = mergeMetrics(metrics, ...
    s12_benchmark_aggregate_qualification(raw.solver));
analysis = struct("metrics", metrics, "plot", struct( ...
    "x", 1, "error", metrics.max_state_error));
end

function result = mergeMetrics(left, right)
result = left;
fields = fieldnames(right);
for index = 1:numel(fields)
    result.(fields{index}) = right.(fields{index});
end
end

function acceptance = acceptCase(metrics)
checks = [ ...
    check("uniform_state_error", metrics.max_state_error, 1e-10), ...
    check("uniform_conservation", metrics.conservation_error, 1e-10), ...
    check("uniform_cfl", metrics.max_courant, 0.45 * (1 + 1e-12))];
acceptance = struct("status", passStatus(checks), "checks", checks);
end

function value = check(id, actual, limit)
value = struct("id", id, "passed", actual <= limit, ...
    "actual", actual, "limit", limit);
end

function status = passStatus(checks)
if all([checks.passed])
    status = "passed";
else
    status = "failed";
end
end

function state = primitiveToConservative(rho, velocity, pressure, gamma)
state = [rho; rho .* velocity; ...
    pressure / (gamma - 1) + 0.5 * rho .* velocity.^2];
end
