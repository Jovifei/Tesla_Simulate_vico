    "model_sha256", modelHash(modelPath), "clipping_count", clipping, ...
    "flux_fallback_count", fallback, "runtime_seconds", raw.runtime_seconds, ...
    "acceptance_limits", config.acceptance_limits, ...
    "threshold_evidence", config.threshold_evidence);
analysis = struct("metrics", metrics, "plot", struct("time", {plotTime}, ...
