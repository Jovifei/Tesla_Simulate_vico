function summary = s12_benchmark_aggregate_qualification(solvers)
%S12_BENCHMARK_AGGREGATE_QUALIFICATION Reduce adapter diagnostics for metrics.
arguments
    solvers (1,:) struct
end
if isempty(solvers) || ~all(isfield(solvers, "qualification"))
    error("S12:Benchmark:QualificationMissing", ...
        "Every successful solver result must contain qualification diagnostics.");
end
diagnostics = [solvers.qualification];
if numel(unique(string({diagnostics.spatial_scheme}))) ~= 1 || ...
        numel(unique(string({diagnostics.limiter}))) ~= 1
    error("S12:Benchmark:QualificationMismatch", ...
        "A benchmark case cannot aggregate mixed reconstruction modes.");
end
limitedCellCount = sum([diagnostics.limited_cell_count]);
sampledCellCount = sum([diagnostics.sampled_cell_count]);
summary = struct( ...
    "spatial_scheme", diagnostics(1).spatial_scheme, ...
    "reconstruction_variables", diagnostics(1).reconstruction_variables, ...
    "limiter", diagnostics(1).limiter, ...
    "limiter_activation_count", sum([diagnostics.limiter_activation_count]), ...
    "limited_cell_fraction", limitedCellCount / max(sampledCellCount, 1), ...
    "minimum_reconstructed_density", min([diagnostics.minimum_reconstructed_density]), ...
    "minimum_reconstructed_pressure", min([diagnostics.minimum_reconstructed_pressure]), ...
    "invalid_reconstruction_count", sum([diagnostics.invalid_reconstruction_count]), ...
    "invalid_stage_count", sum([diagnostics.invalid_stage_count]), ...
    "stage_validation_count", sum([diagnostics.stage_validation_count]), ...
    "end_time_clipping_count", sum([diagnostics.end_time_clipping_count]), ...
    "clipping_count", sum([diagnostics.clipping_count]), ...
    "flux_fallback_count", sum([diagnostics.flux_fallback_count]), ...
    "automatic_retry_count", sum([diagnostics.automatic_retry_count]), ...
    "cfl_changed", any([diagnostics.cfl_changed]));
end
