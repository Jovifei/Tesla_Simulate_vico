    "pressure_amplitude", 100);
cases = [ ...
    transientCase("linear_propagation", "nonreflecting_reference_boundary", 100, 0, 0.00045), ...
    transientCase("closed_rigid_reflection", "closed_rigid_end", 100, 0, 0.0017), ...
    transientCase("pressure_release_reflection", "ideal_pressure_release_open_end", 100, 0, 0.0017), ...
    transientCase("finite_amplitude_pulse", "ideal_pressure_release_open_end", 5000, 0, 0.0017), ...
    transientCase("darcy_attenuation", "nonreflecting_reference_boundary", 100, 0.05, 0.00045)];
config = struct("definition", definition, "cases", cases, ...
    "grid_cell_counts", reshape(settings.grid_cell_counts, 1, []), ...
    "cfl", settings.cfl, "max_steps", settings.max_steps, ...
