function result = s12_benchmark_smooth_periodic(config, cflLimit)
%S12_BENCHMARK_SMOOTH_PERIODIC Run fixed-grid Richardson time convergence.
arguments
    config (1,1) struct
    cflLimit (1,1) double {mustBePositive}
end

gamma = 1.4;
reconstruction = "first_order";
if isfield(config, "reconstruction")
    reconstruction = config.reconstruction;
end
domainLength = 1.0;
velocity = 1.0;
pressure = 1.0;
amplitude = 0.2;
cellCount = config.cell_count;
dx = domainLength / cellCount;
x = ((1:cellCount) - 0.5) * dx;
rho = 1.0 + amplitude * sin(2 * pi * x);
initialState = primitiveToConservative(rho, velocity, pressure, gamma);
dtRequested = config.base_dt ./ reshape(config.dt_divisors, 1, []);
stepCounts = config.end_time ./ dtRequested;
if any(abs(stepCounts - round(stepCounts)) > 32 * eps(max(stepCounts)))
    error("S12:Benchmark:NonintegralTimeGrid", ...
        "Every requested dt must divide the smooth-wave end time exactly.");
end
stepCounts = round(stepCounts);

started = tic;
runs = s12_run_periodic_ssprk3(initialState, gamma, dx, ...
    dtRequested, stepCounts, cflLimit, ...
    Reconstruction=reconstruction);
runtimeSeconds = toc(started);
runCount = numel(runs);
finalStates = cell(1, runCount);
cflClipped = false(1, runCount);
stageDtRatio = zeros(1, runCount);
scaledConservationError = zeros(runCount, 3);
exactError = zeros(1, runCount);
exactRho = 1.0 + amplitude * sin(2 * pi * (x - velocity * config.end_time));
exactState = primitiveToConservative(exactRho, velocity, pressure, gamma);
conservationScale = max(abs(sum(initialState, 2)).', 1);

for runIndex = 1:runCount
    finalStates{runIndex} = runs(runIndex).final_state;
    tolerance = 32 * eps(max(1, dtRequested(runIndex)));
    cflClipped(runIndex) = any(abs(runs(runIndex).used_dt - ...
        dtRequested(runIndex)) > tolerance);
    stageDtRatio(runIndex) = max(runs(runIndex).used_dt) / ...
        min(runs(runIndex).used_dt);
    scaledConservationError(runIndex, :) = ...
        abs(runs(runIndex).conservation_residual) ./ conservationScale;
    exactError(runIndex) = norm(runs(runIndex).final_state - ...
        exactState, "fro") / sqrt(numel(exactState));
end

selfError = zeros(1, runCount - 1);
for errorIndex = 1:(runCount - 1)
    selfError(errorIndex) = norm(finalStates{errorIndex} - ...
        finalStates{errorIndex + 1}, "fro") / sqrt(numel(initialState));
end
observedOrder = log2(selfError(1:end - 1) ./ selfError(2:end));

result = struct( ...
    "id", "smooth_periodic_entropy_wave", ...
    "boundary", "periodic", ...
    "gamma", gamma, ...
    "cell_count", cellCount, ...
    "dx", dx, ...
    "end_time", config.end_time, ...
    "dt_requested", dtRequested, ...
    "step_count", stepCounts, ...
    "cfl_limit", cflLimit, ...
    "reconstruction", reconstruction, ...
    "cfl_clipped", cflClipped, ...
    "stage_dt_ratio", stageDtRatio, ...
    "self_error", selfError, ...
    "observed_order", observedOrder, ...
    "exact_error", exactError, ...
    "scaled_conservation_error", scaledConservationError, ...
    "x", x, ...
    "exact_density", exactRho, ...
    "final_states", {finalStates}, ...
    "qualification", s12_benchmark_aggregate_qualification(runs), ...
    "runtime_seconds", runtimeSeconds);
end

function state = primitiveToConservative(rho, velocity, pressure, gamma)
state = [rho; rho .* velocity; ...
    pressure / (gamma - 1) + 0.5 * rho .* velocity.^2];
end
