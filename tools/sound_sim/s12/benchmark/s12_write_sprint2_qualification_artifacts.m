function result = s12_write_sprint2_qualification_artifacts(result, outputDirectory)
%S12_WRITE_SPRINT2_QUALIFICATION_ARTIFACTS Render views from qualification JSON.
arguments
    result (1,1) struct
    outputDirectory (1,1) string
end
if ~isfolder(outputDirectory)
    mkdir(outputDirectory);
end
validateResult(result);
result.artifacts = [ ...
    artifact("report", "markdown", "benchmark-report.md"), ...
    artifact("manifest", "json", "benchmark-result.json"), ...
    artifact("case_comparison", "csv", "sprint2-case-comparison.csv"), ...
    artifact("smooth_spatial", "csv", "sprint2-smooth-spatial.csv"), ...
    artifact("smooth_spatial_convergence", "png", ...
        "sprint2-smooth-spatial-convergence.png")];
writeCaseComparison(result, fullfile(outputDirectory, result.artifacts(3).path));
writeSpatialComparison(result, fullfile(outputDirectory, result.artifacts(4).path));
writeMarkdown(result, fullfile(outputDirectory, result.artifacts(1).path));
writeSpatialPlot(result, fullfile(outputDirectory, result.artifacts(5).path));
writeText(fullfile(outputDirectory, result.artifacts(2).path), ...
    string(jsonencode(result, "PrettyPrint", true)) + newline);
end

function validateResult(result)
required = ["schema", "schema_minor", "suite", "environment", "cases", ...
    "artifacts", "acceptance"];
if string(result.schema) ~= "benchmark.schema.v1" || ...
        ~all(ismember(required, string(fieldnames(result))))
    error("S12:Benchmark:InvalidQualificationResult", ...
        "The Sprint 2 report input is not a benchmark.schema.v1 minor result.");
end
end

function value = artifact(id, type, path)
value = struct("id", id, "type", type, "path", path);
end

function writeCaseComparison(result, path)
fileId = fopen(path, "wt", "n", "UTF-8");
assert(fileId >= 0, "S12:Benchmark:FileOpen", ...
    "Cannot open Sprint 2 comparison CSV.");
cleanup = onCleanup(@() fclose(fileId));
fprintf(fileId, "%s\n", ...
    "schema_version,case_id,category,spatial_scheme,rho_l1_error,u_l1_error,p_l1_error," + ...
    "feature_position,density_peak_to_trough,total_variation,min_density,min_pressure," + ...
    "min_interface_density,min_interface_pressure,invalid_reconstruction_count," + ...
    "limiter_activation_count,limited_cell_fraction,max_courant,step_count," + ...
    "conservation_error,end_time_clipping_count,clipping_count,flux_fallback_count," + ...
    "automatic_retry_count,cfl_changed");
for index = 1:numel(result.cases)
    comparison = result.cases(index);
    writeComparisonRow(fileId, comparison, "first_order", comparison.first_order);
    writeComparisonRow(fileId, comparison, "muscl_minmod", comparison.muscl_minmod);
end
end

function writeComparisonRow(fileId, comparison, scheme, metrics)
feature = metric(metrics, ["shock_position", "contact_position", ...
    "major_gradient_positions"]);
fprintf(fileId, ...
    "benchmark.schema.v1,%s,%s,%s,%.12g,%.12g,%.12g,%.12g,%.12g,%.12g," + ...
    "%.12g,%.12g,%.12g,%.12g,%.12g,%.12g,%.12g,%.12g,%.12g,%.12g,%.12g," + ...
    "%.12g,%.12g,%.12g,%.12g,%.12g\n", ...
    comparison.id, comparison.category, scheme, ...
    metric(metrics, ["rho_l1_error", "density_l1_error"]), ...
    metric(metrics, "velocity_l1_error"), ...
    metric(metrics, "pressure_l1_error"), feature, ...
    metric(metrics, "density_peak_to_trough"), ...
    metric(metrics, "density_total_variation"), ...
    metric(metrics, "min_density"), metric(metrics, "min_pressure"), ...
    metric(metrics, "minimum_reconstructed_density"), ...
    metric(metrics, "minimum_reconstructed_pressure"), ...
    metric(metrics, "invalid_reconstruction_count"), ...
    metric(metrics, "limiter_activation_count"), ...
    metric(metrics, "limited_cell_fraction"), ...
    metric(metrics, "max_courant"), metric(metrics, "step_count"), ...
    metric(metrics, "conservation_error"), ...
    metric(metrics, "end_time_clipping_count"), ...
    metric(metrics, "clipping_count"), metric(metrics, "flux_fallback_count"), ...
    metric(metrics, "automatic_retry_count"), metric(metrics, "cfl_changed"));
end

function writeSpatialComparison(result, path)
fileId = fopen(path, "wt", "n", "UTF-8");
assert(fileId >= 0, "S12:Benchmark:FileOpen", ...
    "Cannot open Sprint 2 spatial CSV.");
cleanup = onCleanup(@() fclose(fileId));
fprintf(fileId, "%s\n", ...
    "schema_version,spatial_scheme,cell_count,rho_l1_error,u_l1_error,p_l1_error," + ...
    "rho_observed_order,u_observed_order,p_observed_order,requested_dt,effective_dt," + ...
    "cfl_clipped,end_time_clipped,muscl_to_first_rho_error_ratio,cell_averages");
spatial = result.cases(string({result.cases.id}) == "smooth_periodic_entropy_wave_spatial");
for scheme = ["first_order", "muscl_minmod"]
    metrics = spatial.(scheme);
    for index = 1:numel(metrics.cell_counts)
        fprintf(fileId, ...
            "benchmark.schema.v1,%s,%.12g,%.12g,%.12g,%.12g,%.12g,%.12g,%.12g," + ...
            "%.12g,%.12g,%d,%d,%.12g,%d\n", ...
            scheme, metrics.cell_counts(index), metrics.rho_l1_error(index), ...
            metrics.velocity_l1_error(index), metrics.pressure_l1_error(index), ...
            indexed(metrics.rho_observed_order, index), ...
            indexed(metrics.velocity_observed_order, index), ...
            indexed(metrics.pressure_observed_order, index), ...
            metrics.dt_requested(index), metrics.dt_effective(index), ...
            metrics.cfl_clipped(index), metrics.end_time_clipped(index), ...
            indexed(spatial.rho_error_ratio, index), metrics.uses_cell_averages);
    end
end
end

function value = indexed(values, index)
if index <= numel(values)
    value = values(index);
else
    value = NaN;
end
end

function value = metric(metrics, fields)
value = NaN;
for field = fields
    if isfield(metrics, field)
        candidate = metrics.(field);
        if isscalar(candidate)
            value = double(candidate);
        elseif ~isempty(candidate)
            value = double(candidate(1));
        end
        return
    end
end
end

function writeMarkdown(result, path)
lines = [ ...
    "# S12 Sprint 2 MUSCL Final Qualification", "", ...
    "- Schema: `" + string(result.schema) + "` minor `" + ...
        string(result.schema_minor) + "`", ...
    "- Profile: `" + string(result.suite.profile) + "`", ...
    "- Git commit: `" + string(result.environment.git_commit) + "`", ...
    "- MATLAB: `" + string(result.environment.matlab_release) + "`", ...
    "- Acceptance: **" + upper(string(result.acceptance.status)) + "**", "", ...
    "| Case | First-order rho L1 | MUSCL rho L1 | MUSCL/first |", ...
    "|---|---:|---:|---:|"];
for index = 1:numel(result.cases)
    comparison = result.cases(index);
    lines(end + 1) = sprintf("| %s | %.12g | %.12g | %.12g |", ...
        comparison.id, ...
        metric(comparison.first_order, ["rho_l1_error", "density_l1_error"]), ...
        metric(comparison.muscl_minmod, ["rho_l1_error", "density_l1_error"]), ...
        metric(comparison, "rho_error_ratio")); %#ok<AGROW>
end
spatial = result.cases(string({result.cases.id}) == "smooth_periodic_entropy_wave_spatial");
lines(end + 1:end + 8) = ["", "## Smooth Periodic Entropy Wave", ...
    "Cell-average initial data and analytic reference are used at every grid.", ...
    "", "| Scheme | Finest rho order | CFL clipped | End-time clipped |", ...
    "|---|---:|---:|---:|", ...
    sprintf("| first_order | %.12g | %d | %d |", ...
        spatial.first_order.rho_observed_order(end), ...
        any(spatial.first_order.cfl_clipped), any(spatial.first_order.end_time_clipped)), ...
    sprintf("| muscl_minmod | %.12g | %d | %d |", ...
        spatial.muscl_minmod.rho_observed_order(end), ...
        any(spatial.muscl_minmod.cfl_clipped), any(spatial.muscl_minmod.end_time_clipped))];
lines(end + 1:end + 4) = ["", "## Acceptance", "", ...
    "Machine acceptance is stored in `benchmark-result.json`; this report does not recompute it."];
writeText(path, strjoin(lines, newline) + newline);
end

function writeSpatialPlot(result, path)
spatial = result.cases(string({result.cases.id}) == "smooth_periodic_entropy_wave_spatial");
figureHandle = figure("Visible", "off", "Color", "white", ...
    "Position", [100, 100, 800, 500]);
cleanup = onCleanup(@() close(figureHandle));
axesHandle = axes(figureHandle);
loglog(axesHandle, spatial.first_order.cell_counts, ...
    spatial.first_order.rho_l1_error, "o-", "LineWidth", 1.5);
hold(axesHandle, "on");
loglog(axesHandle, spatial.muscl_minmod.cell_counts, ...
    spatial.muscl_minmod.rho_l1_error, "s-", "LineWidth", 1.5);
grid(axesHandle, "on");
xlabel(axesHandle, "cell count");
ylabel(axesHandle, "rho L1 error");
title(axesHandle, "Smooth Periodic Entropy Wave: Spatial Convergence");
legend(axesHandle, ["first_order", "muscl_minmod"], "Location", "southwest");
saveDeterministicFigure(figureHandle, path);
end

function saveDeterministicFigure(figureHandle, path)
temporaryPath = tempname(fileparts(path)) + ".png";
cleanup = onCleanup(@() deleteIfPresent(temporaryPath));
set(figureHandle, "PaperUnits", "inches", ...
    "PaperPosition", [0, 0, 8, 5], "PaperSize", [8, 5], ...
    "InvertHardcopy", "off", "Renderer", "painters");
print(figureHandle, temporaryPath, "-dpng", "-r100", "-vector");
pixels = imread(temporaryPath);
imwrite(pixels, path, "png");
stripPngTimeChunk(path);
end

function writeText(path, content)
fileId = fopen(path, "wt", "n", "UTF-8");
assert(fileId >= 0, "S12:Benchmark:FileOpen", "Cannot open report artifact.");
cleanup = onCleanup(@() fclose(fileId));
fprintf(fileId, "%s", char(content));
end

function deleteIfPresent(path)
if isfile(path)
    delete(path);
end
end

function stripPngTimeChunk(path)
fileId = fopen(path, "rb");
assert(fileId >= 0, "S12:Benchmark:FileOpen", "Cannot read PNG artifact.");
readCleanup = onCleanup(@() fclose(fileId));
bytes = fread(fileId, Inf, "*uint8");
delete(readCleanup);
output = bytes(1:8);
offset = 9;
while offset <= numel(bytes)
    lengthBytes = double(bytes(offset:offset + 3));
    dataLength = lengthBytes.' * [256^3; 256^2; 256; 1];
    chunkEnd = offset + dataLength + 11;
    chunkType = string(char(bytes(offset + 4:offset + 7).'));
    if chunkType ~= "tIME"
        output = [output; bytes(offset:chunkEnd)]; %#ok<AGROW>
    end
    offset = chunkEnd + 1;
end
fileId = fopen(path, "wb");
assert(fileId >= 0, "S12:Benchmark:FileOpen", "Cannot normalize PNG artifact.");
writeCleanup = onCleanup(@() fclose(fileId));
fwrite(fileId, output, "uint8");
end
