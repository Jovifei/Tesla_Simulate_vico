end

function path = relativeModelPath
path = "tools/sound_sim/s12/models/fvm_ref/s12_euler_fvm_transient_wave_ref.slx";
end

function hash = modelHash(path)
[status, output] = system(sprintf('certutil -hashfile "%s" SHA256', path));
if status ~= 0, error("S12:TransientWave:HashFailure", "Cannot hash transient model."); end
hash = upper(string(regexp(output, '[0-9A-Fa-f]{64}', 'match', 'once')));
end

function output = callTransient(functionName, varargin)
root = fileparts(mfilename("fullpath"));
validation = fullfile(fileparts(root), "validation", "transient_wave");
if ~any(string(strsplit(path, pathsep)) == validation)
    addpath(validation); cleanup = onCleanup(@() rmpath(validation));
else
    cleanup = onCleanup(@() []);
end
output = feval(functionName, varargin{:});
clear cleanup
end
