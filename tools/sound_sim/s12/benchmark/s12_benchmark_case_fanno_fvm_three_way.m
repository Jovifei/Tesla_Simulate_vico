        metrics.single_maximum_relative_error <= 0.02 && ...
        metrics.segmented_maximum_relative_error <= 0.002)];
if numel(metrics.grid_cell_counts) > 1 && numel(metrics.lengths_m) > 1
    meanProfileError = mean(metrics.profile_error_l1, 3);
    checks(end + 1) = checkLogical("profile_error_monotone", ...
        all(diff(meanProfileError, 1, 2) < 0, "all"));
    checks(end + 1) = checkLogical("finest_better_than_coarsest", ...
        all(meanProfileError(:, end) < meanProfileError(:, 1)));
    moderateIndex = find(metrics.lengths_m == 76, 1);
    finestOrder = metrics.observed_grid_order(moderateIndex, end);
    checks(end + 1) = checkMinimum("moderate_observed_order", finestOrder, ...
        limits.minimum_moderate_observed_order);
end

status = "failed";
if all([checks.passed])
    status = "passed";
end
acceptance = struct("status", status, "checks", checks);
end

function result = runUniformDecay(config)
gamma = config.physical_definition.gamma;
gasConstant = config.physical_definition.gas_constant;
density = 1.2;
velocity = 50;
pressure = 1e5;
totalEnergy = pressure / (gamma - 1) + 0.5 * density * velocity^2;
initialState = repmat([density; density * velocity; totalEnergy], 1, 8);
adapter = struct("gamma", gamma, "gas_constant", gasConstant, ...
    "length_m", 1, "hydraulic_diameter_m", 0.1, ...
    "darcy_friction_factor", 0.02, "cfl", config.cfl, ...
    "cfl_safety_factor", config.cfl_safety_factor, ...
    "end_time_s", 2e-5, "max_steps", 1, ...
    "boundary", "periodic_uniform_friction", ...
    "initial_state", initialState);
fvm = callValidation("s12_run_fanno_fvm", adapter);
k = adapter.darcy_friction_factor / (2 * adapter.hydraulic_diameter_m);
analyticalVelocity = velocity / (1 + k * abs(velocity) * fvm.final_time_s);
analyticalPressure = (gamma - 1) * (totalEnergy - ...
    0.5 * density * analyticalVelocity^2);
analytical = [density, analyticalVelocity, analyticalPressure, totalEnergy];
finalDensity = fvm.final_state(1, 1);
finalVelocity = fvm.final_state(2, 1) / finalDensity;
finalPressure = (gamma - 1) * (fvm.final_state(3, 1) - ...
    0.5 * finalDensity * finalVelocity^2);
numerical = [finalDensity, finalVelocity, finalPressure, fvm.final_state(3, 1)];
result = struct("initial", [density, velocity, pressure, totalEnergy], ...
    "analytical", analytical, "numerical", numerical, ...
    "relative_error", abs(numerical - analytical) ./ abs(analytical), ...
    "final_time_s", fvm.final_time_s, ...
    "end_time_clipping_count", fvm.qualification.end_time_clipping_count);
end

function result = runColdStart(config)
base = config.physical_definition;
lengthM = config.lengths_m(1);
if any(config.lengths_m == 76)
    lengthM = 76;
end
cellCount = 20;
reference = callValidation("s12_fanno_analytical_profile", ...
    base, lengthM, cellCount, config.quadrature_nodes);
fraction = ((1:cellCount) - 0.5) / cellCount;
inletDensity = base.inlet_static_pressure / ...
    (base.gas_constant * base.inlet_static_temperature);
inletVelocity = (base.mass_flow / base.area) / inletDensity;
outlet = reference.outlet_reference;
density = inletDensity + (outlet.static_density - inletDensity) * fraction;
velocity = inletVelocity + (outlet.velocity - inletVelocity) * fraction;
pressure = base.inlet_static_pressure + ...
    (outlet.static_pressure - base.inlet_static_pressure) * fraction;
initialState = [density; density .* velocity; ...
    pressure / (base.gamma - 1) + 0.5 * density .* velocity.^2];
adapter = adapterConfiguration(config, reference, lengthM);
adapter.initial_state = initialState;
adapter.max_steps = config.maximum_steps;
fvm = callValidation("s12_run_fanno_fvm", adapter);
numerical = primitive(fvm.final_state, base);
analytical = primitive(reference.cell_average_state, base);
[profileL1, ~] = profileErrors(numerical, analytical);
result = struct("length_m", lengthM, "grid_cell_count", cellCount, ...
    "steady_state_reached", fvm.steady_state_reached, ...
    "step_count", fvm.step_count, ...
    "profile_error_l1", reshape(profileL1, 1, []), ...
    "balance_metrics", fvm.balance_metrics);
end

function value = emptyRun
value = struct("length_index", 0, "grid_index", 0, "length_m", 0, ...
    "grid_cell_count", 0, "reference", struct(), "fvm", struct());
end

function primitiveState = primitive(state, definition)
density = state(1, :);
velocity = state(2, :) ./ density;
pressure = (definition.gamma - 1) * (state(3, :) - ...
    0.5 * density .* velocity.^2);
temperature = pressure ./ (density * definition.gas_constant);
mach = velocity ./ sqrt(definition.gamma * definition.gas_constant .* temperature);
primitiveState = struct("density", density, "velocity", velocity, ...
    "pressure", pressure, "temperature", temperature, "mach", mach);
end

function [l1, linf] = profileErrors(numerical, reference)
fields = ["density", "pressure", "temperature", "mach"];
l1 = zeros(1, 1, numel(fields));
linf = zeros(1, 1, numel(fields));
for index = 1:numel(fields)
    expected = reference.(fields(index));
    relative = abs(numerical.(fields(index)) - expected) ./ ...
        max(abs(expected), realmin);
    l1(1, 1, index) = mean(relative);
    linf(1, 1, index) = max(relative);
end
end

function values = outletError(numerical, reference)
actual = [numerical.density, numerical.pressure, ...
    numerical.temperature, numerical.mach];
expected = [reference.static_density, reference.static_pressure, ...
    reference.static_temperature, reference.mach];
values = reshape(abs(actual - expected) ./ abs(expected), 1, 1, []);
end

function orders = observedOrders(errors, cellCounts)
orders = NaN(size(errors, 1), max(size(errors, 2) - 1, 1));
if size(errors, 2) < 2
    return
end
for index = 1:size(errors, 2) - 1
    ratio = cellCounts(index + 1) / cellCounts(index);
    orders(:, index) = log(errors(:, index) ./ errors(:, index + 1)) / log(ratio);
end
end

