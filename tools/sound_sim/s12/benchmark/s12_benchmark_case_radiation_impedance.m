function definition = s12_benchmark_case_radiation_impedance
%S12_BENCHMARK_CASE_RADIATION_IMPEDANCE Define Sprint 4D-A frequency evidence.
definition = struct("configure", @configureCase, "run", @runCase, ...
    "analyze", @analyzeCase, "accept", @acceptCase);
end

function config = configureCase(profile)
settings = profile.radiation_impedance;
physical = callRadiation("s12_radiation_case_definition", ...
    string(settings.validation_profile));
config = struct( ...
    "validation_profile", string(settings.validation_profile), ...
    "physical_definition", physical, ...
    "frequency_csv_path", "radiation-impedance-frequency.csv", ...
    "threshold_evidence", "silva_2009_causal_pade_ka_le_2_and_levine_schwinger_static_limit.v1");
end

function raw = runCase(config)
started = tic;
definition = config.physical_definition;
reference = callRadiation("s12_radiation_unflanged_reference", ...
    definition.fit_validation_ka, definition);
fit = callRadiation("s12_radiation_unflanged_pade_fit", ...
    definition.fit_validation_ka, definition);
package = callRadiation("s12_radiation_boundary_package", definition);
raw = struct("config", config, "reference", reference, "fit", fit, ...
    "package", package, "runtime_seconds", toc(started));
end

function analysis = analyzeCase(raw)
config = raw.config;
definition = config.physical_definition;
reference = raw.reference;
fit = raw.fit;
impedanceRelativeError = abs(fit.normalized_impedance - ...
    reference.normalized_impedance) ./ max(abs(reference.normalized_impedance), 0.02);
reflectionComplexError = abs(fit.reflection - reference.reflection);
reflectionMagnitudeError = abs(abs(fit.reflection) - abs(reference.reflection));
reflectionPhaseError = abs(angle(fit.reflection ./ reference.reflection));
referenceCorrection = reference.end_correction_over_radius;
fitCorrection = endCorrectionFromReflection(fit.reflection, fit.ka);
endCorrectionRelativeError = abs(fitCorrection - referenceCorrection) ./ ...
    max(abs(referenceCorrection), realmin);
referencePassivity = real(reference.normalized_impedance);
fitPassivity = real(fit.normalized_impedance);
metrics = struct( ...
    "radiation_geometry", definition.radiation_geometry, ...
    "flange_condition", definition.flange_condition, ...
    "mean_flow_mach", definition.mean_flow_mach, ...
    "mode_id", definition.mode_id, ...
    "time_harmonic_convention", definition.time_harmonic_convention, ...
    "impedance_definition", definition.impedance_definition, ...
    "normalization_id", definition.normalization_id, ...
    "reference_plane", definition.reference_plane, ...
    "pipe_radius_m", definition.pipe_radius_m, ...
    "cross_section_area_m2", pi * definition.pipe_radius_m^2, ...
    "rho0", definition.rho0, ...
    "c0", definition.c0, ...
    "frequency_csv_path", config.frequency_csv_path, ...
    "frequency_point_count", numel(reference.ka), ...
    "reference_quadrature_reflection_difference", ...
        reference.quadrature.maximum_reflection_difference, ...
    "reference_quadrature_end_correction_difference", ...
        reference.quadrature.maximum_end_correction_difference, ...
    "impedance_relative_error", max(impedanceRelativeError), ...
    "reflection_complex_error", max(reflectionComplexError), ...
    "reflection_complex_error_rms", rms(reflectionComplexError), ...
    "reflection_magnitude_error", max(reflectionMagnitudeError), ...
    "reflection_phase_error", max(reflectionPhaseError), ...
    "passivity_margin", min([referencePassivity, fitPassivity]), ...
    "reference_passivity_margin", min(referencePassivity), ...
    "fit_passivity_margin", min(fitPassivity), ...
    "maximum_reflection_magnitude", max([abs(reference.reflection), ...
        abs(fit.reflection)]), ...
    "fit_order", fit.fit_order, ...
    "fit_poles", fit.poles, ...
    "fit_stability_margin", fit.stability_margin, ...
    "fit_band_ka", fit.fit_band_ka, ...
    "end_correction_reference", referenceCorrection(1), ...
    "end_correction_fit", fitCorrection(1), ...
    "end_correction_relative_error", max(endCorrectionRelativeError), ...
    "reference_method_id", reference.reference_method_id, ...
    "fit_method_id", fit.fit_method_id, ...
    "training_validation_overlap_count", numel(intersect( ...
        definition.fit_training_ka, definition.fit_validation_ka)), ...
    "radiation_boundary_package", raw.package, ...
    "reference_applicability", reference.applicability, ...
    "fit_applicability", fit.applicability, ...
    "runtime_seconds", raw.runtime_seconds, ...
    "acceptance_limits", definition.acceptance, ...
    "threshold_evidence", config.threshold_evidence);
plot = struct( ...
    "ka", reference.ka, ...
    "frequency_hz", reference.frequency_hz, ...
    "reference_normalized_impedance", reference.normalized_impedance, ...
    "fit_normalized_impedance", fit.normalized_impedance, ...
    "reference_reflection", reference.reflection, ...
    "fit_reflection", fit.reflection, ...
    "reference_end_correction", referenceCorrection, ...
    "fit_end_correction", fitCorrection, ...
    "impedance_relative_error", impedanceRelativeError, ...
    "reflection_complex_error", reflectionComplexError, ...
    "reflection_magnitude_error", reflectionMagnitudeError, ...
    "reflection_phase_error", reflectionPhaseError, ...
    "reference_passivity_margin", referencePassivity, ...
    "fit_passivity_margin", fitPassivity);
analysis = struct("metrics", metrics, "plot", plot);
end

function acceptance = acceptCase(metrics)
limits = metrics.acceptance_limits;
checks = [ ...
    checkMaximum("reference_quadrature_reflection", ...
        metrics.reference_quadrature_reflection_difference, ...
        limits.quadrature_reflection_limit), ...
    checkMaximum("reference_quadrature_end_correction", ...
        metrics.reference_quadrature_end_correction_difference, ...
        limits.quadrature_end_correction_limit), ...
    checkMinimum("passivity_margin", metrics.passivity_margin, ...
        -limits.passivity_negative_tolerance), ...
    checkMaximum("reflection_magnitude", metrics.maximum_reflection_magnitude, ...
        1 + limits.reflection_magnitude_tolerance), ...
    checkMinimum("fit_stability_margin", metrics.fit_stability_margin, 0), ...
    checkMaximum("reflection_complex_error", metrics.reflection_complex_error, ...
        limits.maximum_reflection_complex_error), ...
    checkMaximum("reflection_complex_error_rms", ...
        metrics.reflection_complex_error_rms, limits.rms_reflection_complex_error), ...
    checkMaximum("reflection_magnitude_error", ...
        metrics.reflection_magnitude_error, ...
        limits.maximum_reflection_magnitude_error), ...
    checkMaximum("reflection_phase_error", metrics.reflection_phase_error, ...
        limits.maximum_reflection_phase_error_rad), ...
    checkMaximum("end_correction_relative_error", ...
        metrics.end_correction_relative_error, ...
        limits.maximum_end_correction_relative_error), ...
    checkMaximum("training_validation_overlap_count", ...
        metrics.training_validation_overlap_count, 0)];
acceptance = struct("status", "failed", "checks", checks);
if all([checks.passed])
    acceptance.status = "passed";
end
end

function values = endCorrectionFromReflection(reflection, ka)
phase = unwrap(angle(-reflection));
values = -phase ./ (2 * ka);
end

function value = checkMaximum(id, actual, limit)
value = struct("id", string(id), "passed", logical(actual <= limit), ...
    "actual", actual, "limit", limit);
end

function value = checkMinimum(id, actual, limit)
value = struct("id", string(id), "passed", logical(actual >= limit), ...
    "actual", actual, "limit", limit);
end

function output = callRadiation(functionName, varargin)
benchmarkRoot = fileparts(mfilename("fullpath"));
radiationRoot = fullfile(fileparts(benchmarkRoot), "validation", ...
    "radiation_impedance");
pathEntries = string(strsplit(path, pathsep));
if ~any(pathEntries == radiationRoot)
    addpath(radiationRoot);
    cleanup = onCleanup(@() rmpath(radiationRoot));
else
    cleanup = onCleanup(@() []);
end
output = feval(functionName, varargin{:});
clear cleanup
end
