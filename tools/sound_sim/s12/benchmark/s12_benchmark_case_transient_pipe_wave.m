function raw = runCase(config)
started = tic;
runCount = numel(config.cases) * numel(config.grid_cell_counts);
runs = repmat(emptyRun(), 1, runCount);
index = 0;
for caseIndex = 1:numel(config.cases)
    item = config.cases(caseIndex);
    for gridIndex = 1:numel(config.grid_cell_counts)
        cellCount = config.grid_cell_counts(gridIndex);
        localDefinition = config.definition;
        localDefinition.pressure_amplitude = item.amplitude_pa;
        localDefinition.density = localDefinition.ambient_density;
        localDefinition.pipe_length_m = localDefinition.pipe_length;
        localDefinition.pulse_center_m = localDefinition.pulse_center;
        localDefinition.pulse_sigma_m = localDefinition.pulse_sigma;
        localDefinition.pulse_amplitude_pa = item.amplitude_pa;
        state = callTransient("s12_transient_wave_initial_state", localDefinition, cellCount);
        ambient = [localDefinition.ambient_density; 0; ...
            localDefinition.ambient_pressure / (localDefinition.gamma - 1)];
        adapter = struct("gamma", localDefinition.gamma, ...
            "pipe_length_m", localDefinition.pipe_length, ...
            "initial_state", state, "ambient_state", ambient, ...
            "end_time_s", item.end_time_s, "cfl", config.cfl, ...
            "max_steps", config.max_steps, "rho_floor", 1e-8, "p_floor", 1e-3, ...
            "cfl_hard_maximum", 0.5, "left_boundary", ...
            "nonreflecting_reference_boundary", "right_boundary", item.boundary, ...
            "darcy_friction_factor", item.darcy_friction_factor, ...
            "hydraulic_diameter_m", config.hydraulic_diameter_m);
        fvm = callTransient("s12_run_transient_wave_fvm", adapter);
        index = index + 1;
        runs(index) = struct("case_index", caseIndex, "grid_index", gridIndex, ...
            "grid_cell_count", cellCount, "fvm", fvm);
    end
end
simscape = repmat(struct("case_index", 0, "result", struct()), 1, 2);
boundaries = ["closed_rigid_end", "ideal_pressure_release_open_end"];
for index = 1:numel(boundaries)
    caseIndex = find(string({config.cases.boundary}) == boundaries(index), 1);
    item = config.cases(caseIndex);
    waveSpeed = sqrt(config.definition.gamma * config.definition.gas_constant * ...
        config.temperature_k);
    sourceSigma = config.definition.pulse_sigma / waveSpeed;
    sourceCenter = 4 * sourceSigma;
    fvmTimeOrigin = sourceCenter + config.definition.pulse_center / waveSpeed;
    simscapeConfig = struct("boundary", item.boundary, "gamma", config.definition.gamma, ...
        "gas_constant", config.definition.gas_constant, ...
        "pipe_length_m", config.definition.pipe_length, ...
        "ambient_pressure_pa", config.definition.ambient_pressure, ...
        "temperature_k", config.temperature_k, "pulse_amplitude_pa", item.amplitude_pa, ...
        "pulse_start_s", 0, "pulse_end_s", 8 * sourceSigma, ...
        "pulse_center_m", config.definition.pulse_center, ...
        "pulse_sigma_m", config.definition.pulse_sigma, ...
        "source_center_s", sourceCenter, "fvm_time_origin_s", fvmTimeOrigin, ...
        "end_time_s", item.end_time_s + fvmTimeOrigin, ...
        "area_m2", config.area_m2, "hydraulic_diameter_m", config.hydraulic_diameter_m, ...
        "roughness_m", config.roughness_m);
    simscape(index) = struct("case_index", caseIndex, ...
        "result", callTransient("s12_run_transient_wave_simscape", simscapeConfig));
end
raw = struct("config", config, "runs", runs, "simscape", simscape, ...
    "runtime_seconds", toc(started));
end

function analysis = analyzeCase(raw)
config = raw.config;
caseCount = numel(config.cases);
gridCount = numel(config.grid_cell_counts);
arrivalReference = zeros(caseCount, gridCount);
arrivalMeasured = zeros(caseCount, gridCount);
waveSpeedMeasured = zeros(caseCount, gridCount);
waveformL1 = zeros(caseCount, gridCount);
waveformLinf = zeros(caseCount, gridCount);
amplitudeError = zeros(caseCount, gridCount);
phaseError = zeros(caseCount, gridCount);
linearReferenceApplicable = false(caseCount, 1);
pressureReflection = NaN(caseCount, gridCount);
velocityReflection = NaN(caseCount, gridCount);
incidentEnergy = zeros(caseCount, gridCount);
reflectedEnergy = zeros(caseCount, gridCount);
energyResidual = zeros(caseCount, gridCount);
minimumDensity = zeros(caseCount, gridCount);
minimumPressure = zeros(caseCount, gridCount);
maximumCfl = zeros(caseCount, gridCount);
retry = zeros(caseCount, gridCount);
rejected = zeros(caseCount, gridCount);
stepCount = zeros(caseCount, gridCount);
sampleRate = zeros(caseCount, gridCount);
timeResolution = zeros(caseCount, gridCount);
minimumReconstructedDensity = zeros(caseCount, gridCount);
minimumReconstructedPressure = zeros(caseCount, gridCount);
invalidReconstruction = zeros(caseCount, gridCount);
reconstructionActivation = zeros(caseCount, gridCount);
fluxActivation = zeros(caseCount, gridCount);
reconstructionTheta = zeros(caseCount, gridCount);
fluxTheta = zeros(caseCount, gridCount);
minimumAnchorDensity = zeros(caseCount, gridCount);
minimumAnchorPressure = zeros(caseCount, gridCount);
minimumFinalDensity = zeros(caseCount, gridCount);
minimumFinalPressure = zeros(caseCount, gridCount);
invalidStage = zeros(caseCount, gridCount);
clipping = zeros(caseCount, gridCount);
fallback = zeros(caseCount, gridCount);
conservationResidual = zeros(caseCount, gridCount);
modelPath = "";
plotTime = cell(caseCount, gridCount);
plotPressure = cell(caseCount, gridCount);
for runIndex = 1:numel(raw.runs)
    run = raw.runs(runIndex); i = run.case_index; j = run.grid_index;
    item = config.cases(i); fvm = run.fvm;
    probeIndex = config.comparison_probe_index;
    pressure = fvm.trace_pressure_pa(probeIndex, :);
    velocity = fvm.trace_velocity_mps(probeIndex, :);
    isLinearReference = item.amplitude_pa / config.definition.ambient_pressure <= 0.01;
    linearReferenceApplicable(i) = isLinearReference;
    if isLinearReference
        [referencePressure, ~, referenceArrival] = referenceTrace( ...
            config.definition, item, fvm.probe_locations_m(probeIndex), fvm.trace_time_s);
    else
        referencePressure = NaN(size(pressure));
        referenceArrival = NaN;
    end
    measuredArrival = callTransient("s12_transient_wave_arrival_time", ...
        fvm.trace_time_s, pressure - config.definition.ambient_pressure, 0.1);
    arrivalReference(i, j) = referenceArrival;
    arrivalMeasured(i, j) = measuredArrival;
    if isLinearReference
        waveSpeedMeasured(i, j) = sqrt(config.definition.gamma * ...
            config.definition.ambient_pressure / config.definition.ambient_density) * ...
            referenceArrival / measuredArrival;
        waveformL1(i, j) = mean(abs(pressure - referencePressure)) / item.amplitude_pa;
        waveformLinf(i, j) = max(abs(pressure - referencePressure)) / item.amplitude_pa;
        amplitudeError(i, j) = abs(max(abs(pressure - config.definition.ambient_pressure)) - ...
            max(abs(referencePressure - config.definition.ambient_pressure))) / item.amplitude_pa;
        phaseError(i, j) = abs(measuredArrival - referenceArrival) / ...
            max(referenceArrival, realmin);
    else
        waveSpeedMeasured(i, j) = NaN;
        waveformL1(i, j) = NaN;
        waveformLinf(i, j) = NaN;
        amplitudeError(i, j) = NaN;
        phaseError(i, j) = NaN;
    end
    if item.boundary ~= "nonreflecting_reference_boundary"
        windows = reflectionWindows(config.definition, item, fvm.probe_locations_m(probeIndex));
        reflection = callTransient("s12_transient_wave_reflection_metrics", ...
            fvm.trace_time_s, pressure, velocity, windows);
        pressureReflection(i, j) = reflection.pressure_reflection_coefficient;
        velocityReflection(i, j) = reflection.velocity_reflection_coefficient;
        incidentEnergy(i, j) = reflection.incident_energy;
        reflectedEnergy(i, j) = reflection.reflected_energy;
        energyResidual(i, j) = reflection.boundary_energy_residual;
    end
    minimumDensity(i, j) = fvm.qualification.minimum_density;
    minimumPressure(i, j) = fvm.qualification.minimum_pressure;
    maximumCfl(i, j) = fvm.maximum_cfl;
    retry(i, j) = fvm.qualification.retry_count;
    rejected(i, j) = fvm.qualification.rejected_step_count;
    stepCount(i, j) = fvm.step_count;
    sampleRate(i, j) = fvm.step_count / max(fvm.final_time_s, realmin);
    timeResolution(i, j) = max(diff(fvm.trace_time_s));
    minimumReconstructedDensity(i, j) = fvm.qualification.minimum_reconstructed_density;
    minimumReconstructedPressure(i, j) = fvm.qualification.minimum_reconstructed_pressure;
    invalidReconstruction(i, j) = fvm.qualification.invalid_reconstruction_count;
    reconstructionActivation(i, j) = fvm.qualification.reconstruction_pp_activation_count;
    fluxActivation(i, j) = fvm.qualification.flux_pp_activation_count;
    reconstructionTheta(i, j) = fvm.qualification.reconstruction_pp_min_theta;
    fluxTheta(i, j) = fvm.qualification.flux_pp_min_theta;
    minimumAnchorDensity(i, j) = fvm.qualification.minimum_anchor_partial_density;
    minimumAnchorPressure(i, j) = fvm.qualification.minimum_anchor_partial_pressure;
    minimumFinalDensity(i, j) = fvm.qualification.minimum_final_partial_density;
    minimumFinalPressure(i, j) = fvm.qualification.minimum_final_partial_pressure;
    invalidStage(i, j) = fvm.qualification.invalid_stage_count;
    clipping(i, j) = fvm.qualification.clipping_count;
    fallback(i, j) = fvm.qualification.flux_fallback_count;
    conservationResidual(i, j) = max(abs(fvm.conservation_residual));
    modelPath = string(fvm.model_path);
    plotTime{i, j} = fvm.trace_time_s;
    plotPressure{i, j} = pressure;
end
observedOrder = observedOrders(waveformL1, config.grid_cell_counts);
simscapeMetrics = simscapeComparison(raw, arrivalMeasured(:, end), ...
    arrivalReference(:, end), config.definition.ambient_pressure);
metrics = struct( ...
    "reference_type", "linear_acoustics_primary_simscape_secondary", ...
    "reference_applicability", linearReferenceApplicable, ...
    "transient_case_id", string({config.cases.id}), ...
    "boundary_type", string({config.cases.boundary}), ...
    "reference_wave_speed", sqrt(config.definition.gamma * ...
    config.definition.ambient_pressure / config.definition.ambient_density), ...
    "measured_wave_speed", waveSpeedMeasured, ...
    "arrival_time_reference", arrivalReference, ...
    "arrival_time_measured", arrivalMeasured, ...
    "arrival_time_error", arrivalMeasured - arrivalReference, ...
    "pressure_reflection_coefficient", pressureReflection, ...
    "velocity_reflection_coefficient", velocityReflection, ...
    "phase_error", phaseError, "amplitude_error", amplitudeError, ...
    "waveform_l1_error", waveformL1, "waveform_linf_error", waveformLinf, ...
    "incident_energy", incidentEnergy, "reflected_energy", reflectedEnergy, ...
    "transmitted_energy", zeros(caseCount, gridCount), ...
    "boundary_energy_residual", energyResidual, ...
    "probe_locations", raw.runs(1).fvm.probe_locations_m, ...
    "comparison_probe_index", config.comparison_probe_index, ...
    "probe_sampling_id", "accepted_step_boundary_variable_dt.v1", ...
    "sample_rate", sampleRate, "time_resolution", timeResolution, ...
    "grid_cell_count", config.grid_cell_counts, "grid_cell_counts", config.grid_cell_counts, ...
    "observed_grid_order", observedOrder, "minimum_density", minimumDensity, ...
    "minimum_pressure", minimumPressure, "maximum_cfl", maximumCfl, ...
    "step_count", stepCount, "retry_count", retry, "rejected_step_count", rejected, ...
    "minimum_reconstructed_density", minimumReconstructedDensity, ...
    "minimum_reconstructed_pressure", minimumReconstructedPressure, ...
    "invalid_reconstruction_count", invalidReconstruction, ...
    "reconstruction_pp_activation_count", reconstructionActivation, ...
    "flux_pp_activation_count", fluxActivation, ...
    "reconstruction_pp_min_theta", reconstructionTheta, ...
    "flux_pp_min_theta", fluxTheta, ...
    "minimum_anchor_partial_density", minimumAnchorDensity, ...
    "minimum_anchor_partial_pressure", minimumAnchorPressure, ...
    "minimum_final_partial_density", minimumFinalDensity, ...
    "minimum_final_partial_pressure", minimumFinalPressure, ...
    "invalid_stage_count", invalidStage, "conservation_error", conservationResidual, ...
    "spatial_scheme", "muscl_minmod_pp", "balance_law_mode", "euler_or_darcy_transient", ...
    "boundary_id", "transient_characteristic_validation.v1", ...
    "cell_average_reference_id", config.cell_average_reference_id, ...
    "analytical_reference_id", "linear_gaussian_acoustics.v1", ...
    "simscape_open_reference_id", config.simscape_open_boundary_id, ...
    "simscape_closed_reference_id", config.simscape_closed_boundary_id, ...
