    "minimum_stage_pressure", values([27, 29, 31]), ...
    "minimum_cell_density_by_stage", values([26, 28, 30]), ...
    "minimum_cell_pressure_by_stage", values([27, 29, 31]), ...
    "minimum_interface_density_by_stage", values(32:34), ...
    "minimum_interface_pressure_by_stage", values(35:37), ...
    "minimum_anchor_partial_density_by_stage", values(38:40), ...
    "minimum_anchor_partial_pressure_by_stage", values(41:43), ...
    "minimum_final_partial_density_by_stage", values(44:46), ...
    "minimum_final_partial_pressure_by_stage", values(47:49));
end
limiter = "none";
if reconstruction == "muscl_minmod"
    limiter = "minmod";
end
qualification = struct( ...
    "spatial_scheme", reconstruction, ...
    "reconstruction_variables", "primitive_rho_u_p", ...
    "limiter", limiter, ...
    "limiter_activation_count", values(4), ...
    "limited_cell_count", values(5), ...
    "sampled_cell_count", values(6), ...
    "limited_cell_fraction", values(5) / max(values(6), 1), ...
    "minimum_reconstructed_density", values(1), ...
    "minimum_reconstructed_pressure", values(2), ...
    "invalid_reconstruction_count", values(3), ...
    "stage_validation_count", values(7), ...
    "invalid_stage_count", 0, ...
    "end_time_clipping_count", values(8), ...
    "clipping_count", 0, ...
    "flux_fallback_count", 0, ...
    "automatic_retry_count", 0, ...
    "cfl_changed", false);
end

function modelName = reconstructionModel(reconstruction)
switch reconstruction
    case "first_order"
        modelName = "s12_euler_ssprk3_sod_ref";
    case "muscl_minmod"
        modelName = "s12_euler_ssprk3_muscl_minmod_ref";
    case "muscl_minmod_pp"
        modelName = "s12_euler_ssprk3_muscl_minmod_pp_ref";
end
end

function setParameterValue(workspace, name, value)
parameter = workspace.getVariable(name);
if isa(parameter, "Simulink.Parameter")
    parameter.Value = value;
    workspace.assignin(name, parameter);
else
    workspace.assignin(name, value);
end
end

function [density, pressure] = densityPressure(state, gamma)
density = state(1, :);
velocity = state(2, :) ./ density;
pressure = (gamma - 1) * (state(3, :) - 0.5 * density .* velocity .^ 2);
end

function closeControlledModel(modelName)
if bdIsLoaded(modelName)
    close_system(modelName, 0);
end
end
