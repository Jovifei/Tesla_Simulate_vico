function definition = s12_benchmark_case_sod
%S12_BENCHMARK_CASE_SOD Define the long-time Sod case contract.
definition = struct( ...
    "configure", @configureCase, ...
    "run", @runCase, ...
    "analyze", @analyzeCase, ...
    "accept", @acceptCase);
end

function config = configureCase(profile)
config = struct( ...
    "model", "s12_euler_ssprk3_sod_ref.slx", ...
    "gamma", 1.4, ...
    "cell_count", profile.sod.cell_count, ...
    "dx", 1 / profile.sod.cell_count, ...
    "end_time", profile.sod.end_time, ...
    "cfl", profile.cfl_limit, ...
    "max_steps", 10000, ...
    "discontinuity", 0.5);
end

function raw = runCase(config)
x = ((1:config.cell_count) - 0.5) * config.dx;
left = [1, 0, 1];
right = [0.125, 0, 0.1];
isLeft = x < config.discontinuity;
rho = left(1) * isLeft + right(1) * ~isLeft;
velocity = zeros(size(x));
pressure = left(3) * isLeft + right(3) * ~isLeft;
initialState = primitiveToConservative(rho, velocity, pressure, config.gamma);
started = tic;
solver = s12_run_transmissive_ssprk3(initialState, config.gamma, ...
    config.dx, config.end_time, config.cfl, config.max_steps);
[exactRho, exactVelocity, exactPressure] = s12_exact_sod(x, ...
    config.discontinuity, config.end_time, config.gamma, left, right);
raw = struct("config", config, "x", x, "initial_state", initialState, ...
    "solver", solver, "exact_density", exactRho, ...
    "exact_velocity", exactVelocity, "exact_pressure", exactPressure, ...
    "runtime_seconds", toc(started));
end

function analysis = analyzeCase(raw)
rho = raw.solver.final_state(1, :);
velocity = raw.solver.final_state(2, :) ./ rho;
pressure = (raw.config.gamma - 1) * (raw.solver.final_state(3, :) - ...
    0.5 * rho .* velocity.^2);
scale = max(abs(raw.config.dx * sum(raw.initial_state, 2)).', 1);
metrics = struct( ...
    "density_l1_error", mean(abs(rho - raw.exact_density)), ...
    "velocity_l1_error", mean(abs(velocity - raw.exact_velocity)), ...
    "pressure_l1_error", mean(abs(pressure - raw.exact_pressure)), ...
    "min_density", min(rho), ...
    "min_pressure", min(pressure), ...
    "conservation_error", max(abs( ...
        raw.solver.conservation_residual) ./ scale), ...
    "max_courant", raw.solver.max_courant, ...
    "step_count", raw.solver.step_count, ...
    "runtime_seconds", raw.runtime_seconds);
analysis = struct("metrics", metrics, "plot", struct( ...
    "x", raw.x, "numerical_density", rho, ...
    "exact_density", raw.exact_density));
end

function acceptance = acceptCase(metrics)
checks = [ ...
    check("sod_density_l1", metrics.density_l1_error, 0.04), ...
    check("sod_velocity_l1", metrics.velocity_l1_error, 0.05), ...
    check("sod_pressure_l1", metrics.pressure_l1_error, 0.04), ...
    check("sod_conservation", metrics.conservation_error, 1e-10), ...
    check("sod_positive_density", -metrics.min_density, 0), ...
    check("sod_positive_pressure", -metrics.min_pressure, 0)];
acceptance = struct("status", passStatus(checks), "checks", checks);
end

function value = check(id, actual, limit)
value = struct("id", id, "passed", actual <= limit, ...
    "actual", actual, "limit", limit);
end

function status = passStatus(checks)
if all([checks.passed])
    status = "passed";
else
    status = "failed";
end
end

function state = primitiveToConservative(rho, velocity, pressure, gamma)
state = [rho; rho .* velocity; ...
    pressure / (gamma - 1) + 0.5 * rho .* velocity.^2];
end
