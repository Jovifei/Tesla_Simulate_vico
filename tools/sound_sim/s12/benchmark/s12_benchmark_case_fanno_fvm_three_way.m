function hash = modelSha256(path)
if path == "" || ~isfile(path)
    error("S12:Benchmark:MissingModel", "Fanno FVM model hash source is missing.");
end
[status, output] = system(sprintf('certutil -hashfile "%s" SHA256', path));
if status ~= 0
    error("S12:Benchmark:HashFailure", "Cannot hash the Fanno FVM model.");
end
match = regexp(output, '[0-9A-Fa-f]{64}', 'match', 'once');
if isempty(match)
    error("S12:Benchmark:HashFailure", "Cannot parse the Fanno FVM model hash.");
end
hash = upper(string(match));
end

function output = callValidation(functionName, varargin)
benchmarkRoot = fileparts(mfilename("fullpath"));
validationRoot = fullfile(fileparts(benchmarkRoot), "validation", "fanno");
pathEntries = string(strsplit(path, pathsep));
if ~any(pathEntries == validationRoot)
    addpath(validationRoot);
    cleanup = onCleanup(@() rmpath(validationRoot));
else
    cleanup = onCleanup(@() []);
end
output = feval(functionName, varargin{:});
clear cleanup
end
