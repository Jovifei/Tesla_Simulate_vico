function result = s12_benchmark_new_result(profileId, selector, environment)
%S12_BENCHMARK_NEW_RESULT Create the canonical result envelope.
arguments
    profileId (1,1) string
    selector (1,1) string
    environment (1,1) struct
end
root = fileparts(mfilename("fullpath"));
schema = jsondecode(fileread(fullfile(root, "schema", ...
    "benchmark.schema.v1.json")));
result = struct( ...
    "schema", string(schema.schema), ...
    "schema_minor", schema.schema_minor, ...
    "suite", struct("profile", profileId, "selector", selector), ...
    "environment", environment, ...
    "cases", struct([]), ...
    "artifacts", struct([]), ...
    "acceptance", struct("status", "not_evaluated", "checks", struct([])));
end
