function definition = s12_benchmark_case_fanno_pipe_g
%S12_BENCHMARK_CASE_FANNO_PIPE_G Define analytical/Pipe(G) cross-validation.
definition = struct( ...
    "configure", @configureCase, ...
    "run", @runCase, ...
    "analyze", @analyzeCase, ...
    "accept", @acceptCase);
end

function config = configureCase(profile)
validationProfile = string(profile.fanno.validation_profile);
fanno = callValidation("s12_fanno_case_definition", validationProfile);
config = struct( ...
    "profile", validationProfile, ...
    "reference_type", "analytical_fanno_exact_relation", ...
    "friction_convention", fanno.friction_convention, ...
    "lengths", fanno.lengths, ...
    "segment_counts", fanno.segment_counts, ...
    "single_error_limit", fanno.single_error_limit, ...
    "segmented_error_limit", fanno.segmented_error_limit);
end

function raw = runCase(config)
fanno = callValidation("s12_fanno_case_definition", config.profile);
started = tic;
validation = callValidation("s12_run_fanno_pipe_g", fanno);
raw = struct("config", config, "validation", validation, ...
    "runtime_seconds", toc(started));
end

function analysis = analyzeCase(raw)
validation = raw.validation;
referenceMach = arrayfun(@(item) item.outlet.mach, validation.analytical);
referencePressure = arrayfun(@(item) ...
    item.outlet.static_pressure, validation.analytical);
referenceTemperature = arrayfun(@(item) ...
    item.outlet.static_temperature, validation.analytical);
metrics = struct( ...
    "reference_type", raw.config.reference_type, ...
    "physical_assumptions", validation.physical_assumptions, ...
    "friction_convention", validation.friction_convention, ...
    "lengths", validation.case_definition.lengths, ...
    "segment_counts", validation.case_definition.segment_counts, ...
    "reynolds_number", validation.reynolds_number, ...
    "darcy_friction_factor", validation.darcy_friction_factor, ...
    "inlet_mach", validation.inlet_mach, ...
    "analytical_mach", referenceMach, ...
    "analytical_static_pressure", referencePressure, ...
    "analytical_static_temperature", referenceTemperature, ...
    "single_mach", validation.single.mach, ...
    "single_static_pressure", validation.single.static_pressure, ...
    "single_static_temperature", validation.single.static_temperature, ...
    "single_relative_error", validation.single.relative_error, ...
    "single_maximum_relative_error", ...
        validation.single.maximum_relative_error, ...
    "single_steady_relative_span", validation.single.steady_relative_span, ...
    "single_steady", validation.single.steady, ...
    "segmented_mach", validation.segmented.mach, ...
    "segmented_static_pressure", validation.segmented.static_pressure, ...
    "segmented_static_temperature", validation.segmented.static_temperature, ...
    "segmented_relative_error", validation.segmented.relative_error, ...
    "segmented_maximum_relative_error", ...
        validation.segmented.maximum_relative_error, ...
    "segmented_steady_relative_span", ...
        validation.segmented.steady_relative_span, ...
    "segmented_steady", validation.segmented.steady, ...
    "single_error_limit", raw.config.single_error_limit, ...
    "segmented_error_limit", raw.config.segmented_error_limit, ...
    "minimum_choke_margin", validation.minimum_choke_margin, ...
    "retry_count", validation.retry_count, ...
    "runtime_seconds", raw.runtime_seconds);
analysis = struct("metrics", metrics, "plot", struct( ...
    "length", metrics.lengths, ...
    "analytical_mach", metrics.analytical_mach, ...
    "single_mach", metrics.single_mach, ...
    "segmented_mach", metrics.segmented_mach));
end

function acceptance = acceptCase(metrics)
if numel(metrics.lengths) > 1
    improvementRequired = true;
else
    improvementRequired = false;
end
checks = [ ...
    check("single_pipe_accuracy", metrics.single_maximum_relative_error, ...
        selectLimit(metrics, "single")), ...
    check("segmented_pipe_accuracy", ...
        metrics.segmented_maximum_relative_error, ...
        selectLimit(metrics, "segmented")), ...
    checkMinimum("positive_choke_margin", metrics.minimum_choke_margin, 0), ...
    check("steady_single_pipe", double(~metrics.single_steady), 0), ...
    check("steady_segmented_pipe", double(~metrics.segmented_steady), 0), ...
    check("no_retry", metrics.retry_count, 0)];
if improvementRequired
    checks(end + 1) = check("segmentation_improves_accuracy", ...
        metrics.segmented_maximum_relative_error, ...
        metrics.single_maximum_relative_error);
end
if all([checks.passed])
    status = "passed";
else
    status = "failed";
end
acceptance = struct("status", status, "checks", checks);
end

function limit = selectLimit(metrics, kind)
if kind == "single"
    limit = metrics.single_error_limit;
else
    limit = metrics.segmented_error_limit;
end
end

function value = check(id, actual, limit)
value = struct("id", id, "passed", actual <= limit, ...
    "actual", actual, "limit", limit);
end

function value = checkMinimum(id, actual, minimum)
value = struct("id", id, "passed", actual > minimum, ...
    "actual", actual, "limit", minimum);
end

function output = callValidation(functionName, input)
benchmarkRoot = fileparts(mfilename("fullpath"));
validationRoot = fullfile(fileparts(benchmarkRoot), "validation", "fanno");
pathEntries = string(strsplit(path, pathsep));
if ~any(pathEntries == validationRoot)
    addpath(validationRoot);
    cleanup = onCleanup(@() rmpath(validationRoot));
else
    cleanup = onCleanup(@() []);
end
output = feval(functionName, input);
clear cleanup
end
