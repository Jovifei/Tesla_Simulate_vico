function result = s12_run_transmissive_ssprk3(initialState, gamma, dx, ...
        endTime, cfl, maxSteps, options)
%S12_RUN_TRANSMISSIVE_SSPRK3 Run the validated long-time SSP-RK3 model.
arguments
    initialState (3,:) double
    gamma (1,1) double {mustBeGreaterThan(gamma, 1)}
    dx (1,1) double {mustBePositive}
    endTime (1,1) double {mustBeNonnegative}
    cfl (1,1) double {mustBePositive}
    maxSteps (1,1) double {mustBeInteger, mustBePositive}
    options.Reconstruction (1,1) string {mustBeMember( ...
        options.Reconstruction, ["first_order", "muscl_minmod"])} = "first_order"
end
benchmarkRoot = fileparts(mfilename("fullpath"));
s12Root = fileparts(benchmarkRoot);
modelRoot = fullfile(s12Root, "models", "fvm_ref");
modelName = reconstructionModel(options.Reconstruction);
if bdIsLoaded(modelName)
    close_system(modelName, 0);
end
load_system(fullfile(modelRoot, modelName + ".slx"));
cleanup = onCleanup(@() closeControlledModel(modelName));
workspace = get_param(modelName, "ModelWorkspace");
setParameterValue(workspace, "S12_RK3_State", initialState);
setParameterValue(workspace, "S12_RK3_Gamma", gamma);
setParameterValue(workspace, "S12_RK3_Dx", dx);
setParameterValue(workspace, "S12_RK3_EndTime", endTime);
setParameterValue(workspace, "S12_RK3_CFL", cfl);
setParameterValue(workspace, "S12_RK3_MaxSteps", maxSteps);
output = sim(modelName);
qualification = qualificationResult(output.S12_RK3QualificationDiagnostics, ...
    options.Reconstruction);
result = struct( ...
    "final_state", squeeze(output.S12_RK3FinalState), ...
    "final_time", output.S12_RK3FinalTime(end), ...
    "step_count", output.S12_RK3StepCount(end), ...
    "max_courant", output.S12_RK3MaxCourant(end), ...
    "conservation_residual", reshape( ...
        squeeze(output.S12_RK3ConservationResidual), 1, []), ...
    "boundary", "transmissive", ...
    "reconstruction", options.Reconstruction, ...
    "qualification", qualification);
end

function qualification = qualificationResult(trace, reconstruction)
values = reshape(squeeze(trace), 1, []);
if numel(values) ~= 8
    error("S12:Benchmark:QualificationTrace", ...
        "The SSP-RK3 qualification trace must contain eight diagnostics.");
end
limiter = "none";
if reconstruction == "muscl_minmod"
    limiter = "minmod";
end
qualification = struct( ...
    "spatial_scheme", reconstruction, ...
    "reconstruction_variables", "primitive_rho_u_p", ...
    "limiter", limiter, ...
    "limiter_activation_count", values(4), ...
    "limited_cell_count", values(5), ...
    "sampled_cell_count", values(6), ...
    "limited_cell_fraction", values(5) / max(values(6), 1), ...
    "minimum_reconstructed_density", values(1), ...
    "minimum_reconstructed_pressure", values(2), ...
    "invalid_reconstruction_count", values(3), ...
    "stage_validation_count", values(7), ...
    "invalid_stage_count", 0, ...
    "end_time_clipping_count", values(8), ...
    "clipping_count", 0, ...
    "flux_fallback_count", 0, ...
    "automatic_retry_count", 0, ...
    "cfl_changed", false);
end

function modelName = reconstructionModel(reconstruction)
switch reconstruction
    case "first_order"
        modelName = "s12_euler_ssprk3_sod_ref";
    case "muscl_minmod"
        modelName = "s12_euler_ssprk3_muscl_minmod_ref";
end
end

function setParameterValue(workspace, name, value)
parameter = workspace.getVariable(name);
parameter.Value = value;
workspace.assignin(name, parameter);
end

function closeControlledModel(modelName)
if bdIsLoaded(modelName)
    close_system(modelName, 0);
end
end
