function definition = s12_benchmark_case_double_rarefaction
%S12_BENCHMARK_CASE_DOUBLE_RAREFACTION Define the exact-vacuum stress case.
definition = struct( ...
    "configure", @configureCase, ...
    "run", @runCase, ...
    "analyze", @analyzeCase, ...
    "accept", @acceptCase);
end

function config = configureCase(profile)
config = struct( ...
    "model", "s12_euler_ssprk3_muscl_minmod_pp_ref.slx", ...
    "gamma", 1.4, ...
    "domain", [0, 1], ...
    "discontinuity", 0.5, ...
    "left", [1, -2, 0.1], ...
    "right", [1, 2, 0.1], ...
    "cell_count", profile.double_rarefaction.cell_count, ...
    "end_time", profile.double_rarefaction.end_time, ...
    "require_pp_activation", profile.double_rarefaction.require_pp_activation, ...
    "cfl", profile.cfl_limit, ...
    "reconstruction", profile.reconstruction, ...
    "max_steps", 20000, ...
    "reference_type", "literature_definition_exact_vacuum_stress", ...
    "boundary", "transmissive");
if config.reconstruction ~= "muscl_minmod_pp"
    error("S12:Benchmark:UnsupportedScheme", ...
        "double_rarefaction requires muscl_minmod_pp.");
end
end

function raw = runCase(config)
dx = diff(config.domain) / config.cell_count;
x = config.domain(1) + ((1:config.cell_count) - 0.5) * dx;
isLeft = x < config.discontinuity;
rho = config.left(1) * isLeft + config.right(1) * ~isLeft;
velocity = config.left(2) * isLeft + config.right(2) * ~isLeft;
pressure = config.left(3) * isLeft + config.right(3) * ~isLeft;
initialState = s12_benchmark_primitive_to_conservative( ...
    rho, velocity, pressure, config.gamma);
started = tic;
solver = s12_run_transmissive_ssprk3(initialState, config.gamma, dx, ...
    config.end_time, config.cfl, config.max_steps, ...
    Reconstruction=config.reconstruction);
raw = struct("config", config, "x", x, "dx", dx, ...
    "initial_state", initialState, "solver", solver, ...
    "runtime_seconds", toc(started));
end

function analysis = analyzeCase(raw)
[rho, velocity, pressure] = s12_benchmark_conservative_to_primitive( ...
    raw.solver.final_state, raw.config.gamma);
scale = max(abs(raw.dx * sum(raw.initial_state, 2)).', 1);
metrics = struct( ...
    "reference_type", raw.config.reference_type, ...
    "exact_vacuum_stress", true, ...
    "require_pp_activation", raw.config.require_pp_activation, ...
    "min_density", min(rho), ...
    "min_pressure", min(pressure), ...
    "center_density", centerValue(rho), ...
    "center_pressure", centerValue(pressure), ...
    "near_vacuum_position_range", nearVacuumRange(raw.x, rho), ...
    "density_total_variation", sum(abs(diff(rho))), ...
    "has_nonfinite", any(~isfinite([rho, velocity, pressure]), "all"), ...
    "conservation_error", max(abs( ...
        raw.solver.conservation_residual) ./ scale), ...
    "max_courant", raw.solver.max_courant, ...
    "step_count", raw.solver.step_count, ...
    "runtime_seconds", raw.runtime_seconds);
metrics = merge(metrics, s12_benchmark_aggregate_qualification(raw.solver));
analysis = struct("metrics", metrics, "plot", struct( ...
    "x", raw.x, "numerical_density", rho, ...
    "numerical_pressure", pressure));
end

function acceptance = acceptCase(metrics)
ppActivated = metrics.reconstruction_pp_activation_count > 0 || ...
    metrics.flux_pp_activation_count > 0;
stagePositive = all(metrics.minimum_cell_density_by_stage >= metrics.rho_floor) && ...
    all(metrics.minimum_cell_pressure_by_stage >= metrics.p_floor) && ...
    all(metrics.minimum_interface_density_by_stage >= metrics.rho_floor) && ...
    all(metrics.minimum_interface_pressure_by_stage >= metrics.p_floor) && ...
    all(metrics.minimum_anchor_partial_density_by_stage >= metrics.rho_floor) && ...
    all(metrics.minimum_anchor_partial_pressure_by_stage >= metrics.p_floor) && ...
    all(metrics.minimum_final_partial_density_by_stage >= metrics.rho_floor) && ...
    all(metrics.minimum_final_partial_pressure_by_stage >= metrics.p_floor);
checks = [ ...
    check("double_rarefaction_finite", ~metrics.has_nonfinite), ...
    check("double_rarefaction_stage_positive", stagePositive), ...
    check("double_rarefaction_pp_activated", ...
        ~metrics.require_pp_activation || ppActivated), ...
    check("double_rarefaction_conservation", metrics.conservation_error <= 1e-10), ...
    check("double_rarefaction_cfl", metrics.max_courant <= 0.5 * (1 + 1e-12)), ...
    check("double_rarefaction_no_invalid_stage", metrics.invalid_stage_count == 0), ...
    check("double_rarefaction_no_clipping", metrics.clipping_count == 0), ...
    check("double_rarefaction_no_fallback", metrics.flux_fallback_count == 0), ...
    check("double_rarefaction_no_nominal_retry", ...
        metrics.rejected_step_count == 0 && metrics.retry_count == 0)];
if all([checks.passed])
    status = "passed";
else
    status = "failed";
end
acceptance = struct("status", status, "checks", checks);
end

function value = centerValue(values)
count = numel(values);
middle = floor((count + 1) / 2):ceil((count + 1) / 2);
value = min(values(middle));
end

function range = nearVacuumRange(x, rho)
threshold = min(rho) + 0.05 * (max(rho) - min(rho));
mask = rho <= threshold;
if any(mask)
    range = [min(x(mask)), max(x(mask))];
else
    range = [NaN, NaN];
end
end

function result = merge(left, right)
result = left;
fields = fieldnames(right);
for index = 1:numel(fields)
    result.(fields{index}) = right.(fields{index});
end
end

function value = check(id, passed)
value = struct("id", id, "passed", logical(passed), ...
    "actual", double(~passed), "limit", 0);
end
